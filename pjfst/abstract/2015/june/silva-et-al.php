			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The umbu (Spondias tuberosa Arr. Cam.), Tropical plant native to the Northeast of Brazil, has great potential as exotic fruit in both internal and external markets, which motivated this study undertaken with fruits purchased in the market Santana do Ipanema, picked one day before analysis in a physiologically ripe condition, which were then immersed for one hour in calcium chloride solution at concentrations of T1 (0% CaCl 2), T2 (3% CaCl2), T3 (6% CaCl 2), T4 (9% CaCl2) and T5 (12% CaCl2). After soaking, the fruits were subjected to respiration, were weighed before and after respiration, immediately after which the fruits were placed in a plastic tray and under a PVC film and stored for seven days under refrigeration (15 ° C + 1 ° C and 52%). After seven days, the respiration process was repeated. The following characteristics of the fruits were evaluated: respiratory activity, humidity, Total Soluble Solids TSS, Total Titratable Acidity TTA, TSS / TTA, pH. Ca did not affect the respiratory activity of umbu, but influenced significantly the storage period. The characterization analysis obtained the following results: sample mean humidity of the fruits 88.62%; sample mean TSS 10.58 ° Brix with a significant difference between the means for T1 (a), T2 (a) and T3 (b), T4 (b) and T5 (b); sample mean TAT of 0.30 g citric acid / 100 g of pulp; sample mean TSS / TTA of 2.34 and the mean pH of the fruits under all treatments of 2.75. 
			</p>
