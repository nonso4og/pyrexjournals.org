			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Background: Essential fatty acids are polyunsaturated fatty acids (PUFA) which contain more than one double bond. The purpose of this study was to investigate the level of fatty acid composition in plasma phosphatidylcholine of pregnant Sudanese women and healthy control. There were no records about the levels of these fatty acids in Sudanese people. Material and Methods: Blood samples were obtained from the pregnant women at delivery time in Khartoum state hospitals. Full and detailed history and examinations were performed in the study groups. Plasma lipids were extracted by Folch method and separated by Gas Liquid Chromatography (GLC). Results: These results were expressed as mean and standard deviations. Regarding saturated fatty acids (palmitic acid)was higher among pregnant ladieswhile (staeric, myristic and arachidic) were higher among the control group ( p&#8804;0.001).There were significantly higher level of Docosapentaenoic acid (DPA), Docosahexaenoic acid (DHA), linoleic acid (L.A), Arachidonic acid (A.A), total omega-3 (w-3), total omega-6 (w-6) and total w-3/total w-6 ratio (w-3/w6) ratio in control (p&#8804;0.001), whereas significantly higher level of alpha linolenic acid (ALA), Di homogamma linoleic acid (DHGLA) and Adrenic Acid in pregnant female (p&#8804;0.001). Conclusion: The levels of essential fatty acids in our groups were lower than the international levels, except for LA which was higher in comparison to some countries and lower in comparison to theirs. There were differences in concentration of essential fatty acids between pregnant and non pregnant females. The DHA level of the pregnant women was the lowest level being measured which affects the neonatal DHA level.

			</p>
