			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This descriptive type of cross sectional study was carried out to find out the pattern of smokeless
tobacco use among the women in old Dhaka, during April to June 2012.A total of 105 women using
smokeless tobacco were selected by purposive sampling technique. Data was collected by a structured
pretested questionnaire by face to face interview. A total of 105 respondents reported smokeless
tobacco consumption. Among them smokeless tobacco use was 36.2% in the age group of 31-40 years.
Most of the smokeless tobacco users 73.3% were illiterate. Regular use of smokeless tobacco was
74.3% and occasional use was 25.7%. About 63% were house wives and 41.9 % user had monthly family
income of Taka 5000-10000. Among them 45.3% used zarda, 27.2% used Kahini, 17.1% used sada pata
and10.5% used gul. The initiation of smokeless tobacco use was from others members of the family
44.8%. The reason behind using smokeless tobacco was stated as fun by 40% respondents. Almost
68.6 % respondents think smokeless tobacco do not cause any harm in the oral cavity while only 31.4 %
stated smokeless tobacco cause harm in the oral cavity. There was significant association between
occupation and regular use of smokeless tobacco (p < 0.05). From the study finding it may be
concluded that zarda is mostly used by the women as smokeless tobacco which is harmful for general
health and oral health as well. Mass media should be involved to address the injurious use of
smokeless tobacco on oral health which though known is ignored by the general population.


  
