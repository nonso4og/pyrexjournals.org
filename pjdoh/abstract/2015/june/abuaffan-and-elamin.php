			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Background: Proper oral hygiene performance is a prerequisite to prevent complications from increased food debris and plaque retention during the treatment period with fixed orthodontic appliance. 
Objective: To assess oral hygiene performance of Sudanese patients with fixed orthodontic appliance.
Materials and Methods: A descriptive cross-sectional study of 500 orthodontic patients. Data collected through personal interview, using a well-structured questionnaire regarding oral hygiene maintainer during the fixed orthodontic treatment. 
Results: Half of the sample brushes their teeth twice a day. 46% maintained oral hygiene using a toothbrush and Interdental cleaning-aids. 73% brushes their teeth both vertically and horizontally and 44% used Interdental cleaning-aids daily. 51% preferred the use of Interdental brushes. 38% of the patients attended orthodontic clinics regularly for a check-up and professional cleaning. Most of the patients (81%) taught by their orthodontist to maintain a proper oral hygiene, (61%) were instructed to use fluoridated toothpaste or mouthwash and (79%) of the patients were reported to follow the orthodontist’s instructions for maintaining proper oral hygiene during treatment.
Conclusion: Acceptable oral hygiene maintenance reported in Sudanese patients with fixed orthodontic appliance. 
Recommendation: Great emphasis should be considered to improve the awareness and knowledge of disadvantage of improper oral hygiene maintenance during treatment with fixed orthodontic appliance.


  
			</p>
