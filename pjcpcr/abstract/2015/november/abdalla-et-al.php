			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Multiple myeloma is a haematological malignancy characterized by neoplastic proliferation of a single clone of plasma cells producing a monoclonal immunoglobulin. This clone of plasma cells proliferates in the bone marrow and often results in extensive skeletal destruction with osteolytic lesions, osteopenia, and/or pathologic fractures. Most patients with MM present with signs or symptoms related to the infiltration of plasma cells into the bone marrow, kidney damage or other organs from excess light chains. The aim of this study is to determine the haematological features of Multiple Myeloma in Sudanese patients.
Methods: This is a descriptive cross-sectional hospital based study, was conducted in Radiotherapy and Isotpes Centre Khartoum (RICK), 49 patients with multiple myeloma were included in the study. Hb, TWBCs, ESR and platelets count were done using an automated haematological analyzer (Sysmex). Peripheral blood films were done and bone marrow biopsies were performed to all participants.
Results: showed that Hb levels of less than 11g/dl was found in 87% in females, and less than 13 g/dl was found in all males. While 13% showed normal haemoglobin. Leucopenia was found in 18.4% of patients while 4.1% have leucocytosis. 16.3% of patients have thrombocytopeania and 10.2% have thrombocytosis. Most of the patients (48.98%) have an ESR of 100 mm/hour or more.
Conclusion: Anaemia was found in all male patients and most of the female ones. Other haematological finding shows leucopenia, leucocytosis, thrombocytopenia, and thrombocytosis. Almost all patients showed have plasma cells in bone marrow biopsy. High ESR and high serum protein were common. Most of the patients showed a monoclonal band (M band) on SPEP. Normal serum protein electrophoresis was found in a few patients.

  
			</p>
