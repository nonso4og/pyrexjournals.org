			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Technology developed nowadays quickly affects to the deviant behavior of children. It proved by the increase of sexual crime from 2010 until 2014 (around 2124 cases). In average, it increases about 329 cases in 2011, 746 cases in 2012, 590 cases in 2013, and 459 cases in 2014. Cyber sexual crime becomes the highest category. There are 42 child offenders in cyber sexual crime, 163 children become victims of social media pornography, and 64 child offenders in social media pornography. The problems of sexual crime affect psychology and the growth of children and somehow it’s traumatic to children. Sexual crime gives both positive and negative effects to the children. The positive thing is children will get closer to God, while the negative is children will hold a grudge which leads to revenge, even they becoming the offender. Sexual addiction happens because children feel comfort, happiness, and so it is possible for them to become sex offenders because the addicted to it. It is necessary to conduct addictive counseling and psychology recovery rehabilitation to return the children to their own self. Addiction counseling is done using the strategic humanistic approach, behavior and cognitive behavior therapy.
			</p>
