			<h1 style="font-size:15px; font-weight:600; color:#333333;" class="box-header-blue">The Psychometric Parameters of the Spanish Form of the Arabic Obsessive Compulsive Scale (S-AOCS)</h1>
			
			<br />
			
			<h1 style="font-size:15px; font-weight:600; color:#333333;" class="box-header-blue">Ahmed M. Abdel-Khalek<sup>1*</sup>, Juana Gomez-Benito<sup>2</sup>, Joaquin Tomás-Sábado<sup>3</sup> and Georgina Guilera Ferré<sup>2</sup>

