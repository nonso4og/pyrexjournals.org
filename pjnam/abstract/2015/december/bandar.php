			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Background and Methods: The majorities of young people have no healthy dietary or exercise habits. In the current study, we investigated the associations between dietary habits and body mass index among Hail university students in Saudi Arabia. A cross-sectional study was conducted among university students. Data were collected on a self-completed questionnaire on weight, height and physical activity. Statistical Package for Social Sciences (SPSS) version 13 was used for statistical analysis. Results: The numbers of Participants were 100 males and 100 females aged 18–23years.57% of females were within normal weight, 20% overweight and 23% obese. 48% of males were within normal weight, 28% overweight and 24% obese.17.9% of university students were within a high level of physical activity,40.5% and 41.6%) moderate and low level of physical activity respectively. There was no significant correlation was found between BMI and PA (p>0.5).Despite that, there was a statistically significant correlation was found between physical activity and BMI within normal BMI participants positively (p<0.5). There were no significant differences between males and females. Conclusions: Obesity is a complex disease resulting from complex interactions between genetic and environmental factors such as correlations of gut microbiota composition with diet. Recommendation: Importance of understanding the genetic and environmental interactions contributing to obesity in developing novel therapies and preventive strategies.
  
			</p>
		<p>
<b>Keywords:</b> Obesity, body mass index, physical activity, overweight.
