			<h1 style="font-size:15px; font-weight:600; color:#333333;" class="box-header-blue">Assessing the Association between Nutritional Status and Asymptomatic Malaria Parasitaemia of Children Under Five Years in Ghana</h1>
			
			<br />
			
			<h1 style="font-size:15px; font-weight:600; color:#333333;" class="box-header-blue">Daniel Ayine Nyaaba<sup>1*</sup>, Patricia K. Brown<sup>2</sup> and Jacob K. Agbenorhevi<sup>3</sup></h1>

