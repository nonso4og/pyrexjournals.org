			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The increasing awareness and use of information for different purposes have resulted in the establishment of various information service providers which have the capacity to compete for survival with libraries in providing users with timely, adequate and relevant information in the preferred formats. Information professionals therefore need to respond to such challenges in the quest to enhance the use of their resources and services. This paper therefore looks at the competitive nature of the information environment, identifies the library competitors, rationales for promoting library operations and services, skills acquisition by information professionals as strategies for client retention in libraries. The paper recommends that: libraries should start thinking of plans or modalities to carry out research on customer survey; the need for library managers to be aggressive in soliciting for funds for library support. The paper also recommends the regular organization of training and development programme for skills acquisition by information professionals.
  
			</p>
