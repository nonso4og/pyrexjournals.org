			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This study aims to ascertain gender and techno-phobia among library staff in some selected polytechnic libraries in the south- south region of Nigeria. The study employed a descriptive survey method and questionnaire was the instrument used for data collection. The population of the study consisted of a focus group made up of the total number of one hundred and forty-eight (148) library staff. It was revealed from the study that female library staff are more prone to techno-phobia than their male counterparts, a majority of both sexes agreed that lack of training, lack of computer knowledge, fear and anxiety, network problem and leaning unwanted new skills are the major causes of techno-phobia, poor performance, low productivity, lack of commitment and Job dissatisfaction are the major effect of techno-phobia in the libraries as agreed upon by the both the males and females library staff. The study revealed a significant difference in the level of techno-phobia experienced by male and female library staff. The study concludes that techno-phobia in polytechnic libraries, if not properly manage could bring about poor performance, low productivity, lack of commitment and job dissatisfaction. The study recommends that the polytechnic management and library administrators should deem it necessary to always organize training and retraining programs for all the staff in the library in order to expose them to new and advanced technologies and their usage.
  
			</p>
