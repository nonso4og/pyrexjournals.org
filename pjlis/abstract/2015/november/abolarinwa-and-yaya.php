			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This paper deals on the utilization of competitive intelligence by health information workers of the Nigerian Institute of Medical Research (NMIR).Health information workers’ (Library personnel) knowledge and adaptation of competitive intelligence in library service is considered a very important requirement for efficient and effective service delivery in a library because a library is an essential element in the educational and research endeavor of research institute most especially a medical research institute that require painstaking research activities considering the human life involved. Therefore, health information workers should understand their clients’ needs make the best use of their competitive intelligence, plan service provision, promote the available services, and deliver them efficiently and effectively. The paper recommends that more attention should be paid to utilization of competitive intelligence in library services which will invariably enhance planning and implementation of marketing policy of libraries for effective and efficient library services.
  
			</p>
