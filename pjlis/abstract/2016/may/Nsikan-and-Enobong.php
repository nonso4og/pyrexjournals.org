			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Insurgency, terrorism and other criminal activities are plaguing the Nigerian socio-political environment. Among other things, these are crime against humanity, but some seem to wear the cloaks of religion, politics, ethnic discrimination, territorial encroachment etc, with no absolute conflict philosophy or objective. This work examines the nature, sources, causes and kinds of conflict, especially the ones affecting the Nigerian economy. Conflict has also been identified to be endogenous (emotional and within the system) and exogenous (external and between systems). Appropriate strategies and media for showcasing, reporting and containing conflict situations in Nigeria have been recommended for proper management, reduction and if possible resolution. These media are both traditional and modern. However, the work recommends that there should be no negotiation with inhuman, mindless and faceless organizations, no matter the level and manner of insurgency, terrorism and other conflict situations.  
			</p>
		<p>
<b>Keywords:</b> Conflict, communication, organizations.
