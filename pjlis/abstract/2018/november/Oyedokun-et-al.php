			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Information is a fact that provides answers to queries and some kind of resolve to an uncertainty that can cause changes in human perspective as regard to any state of affairs or happenings in the country. But in a country like Nigeria where social unrest as well as insurgency becomes the bane and persist occurrence; and several studies attested to the fact that information is an agent of transformation as well as sharper of minds and perspectives that direct human actions and directions. One is now left with a phenomenon of who, what, where, when and how (W4H) of such information could be deployed for alternative dispute resolution and curbing of social unrest in our society. Without any gainsaying, confirmation from the literature as well as researchers critical observation confirmed that libraries did not only provide access to information but also make provision for an avenue where patrons can investigate subjectively issues that bothered about national security. It is on this submission and solid ground that this current study reviewed the literature on roles of libraries in the provision of information services for alternative dispute resolution. Literature has it that libraries contribute positively to dispute resolution in the society through their service of providing access to relevant and timely information to library clientele. A recommendation was given that for libraries to be able to act effectively on dispute resolution through their information resources and services, they needed to incorporate the use of ICT tool for quick and efficient information service delivery. It was also recommended that selective dissemination of information to appropriate quarters will curb social unrest as each group will be rightly be informed what needed to be known on government policies. Since there are attestations from the literature on the important roles libraries play in resolving a dispute in the society, the researchers then suggest ways in which such information could be effectively be disseminated, which include outreach programs for remote users as well as a Champaign program for sensitization of citizen on national security.
  
			</p>
