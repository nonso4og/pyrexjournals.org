			<br />
			<br />
			
			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The literature on the impact of managerial self-interest on capital structure has already been  developed   in  developed  markets  that  have  different  institutional  financing arrangements  from  those  in  emerging  markets.  Therefore this requires a thorough examination of the impact of managerial self-interest on corporate capital structure in developing market. In addition, the study so far has been conducted on listed firms with different proxies of managerial self-interest as indicators of capital structure. This  thesis  deals  with  the  impact  of  managerial  self-interest  on  corporate  capital structure of eight selected companies in Addis Ababa. The study has examined the impact of managerial self-interest on corporate capital structure using quantitative methods of inquiry. Mainly the study focuses on the questionnaires analysis so  as  to  see  the representative  proxies  of  managerial  self-interest  such  as,  board independence, managerial shareholding and institutional shareholding, on firms capital structure decision. In addition, performance, risk, company size and tangibility of assets were regressed using ordinary least square (OLS) as a supportive variable in order to compromise the possible deficiencies that might appear in questionnaire analysis. In its main finding the result shows the inverse relationship between managerial shareholding and capital structure in case of banks and  manufacturing companies, and  relatively positive relationship in case of insurance companies under consideration. This is possibly because of the presence of principal shareholders and management’s attitude towards maximizing the company’s profit and obtaining tax benefits for employing long term debt. 
			</p>
		<p>
<b>Keywords:</b> Capital structure, performance, leverage, managerial self interest.  
</p>

