			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The importance of biology studies in the life of individuals cannot be underrated. Recently, in Nigeria Biology that was previously a compulsory subject for all senior secondary school students has been made non compulsory especially for non-sciences students. This development comes with some worries for science educators, especially as it concerns the capacity building and application of the subject in the life of all. This study therefore, investigated the exclusion of biology from non-science senior secondary School students and it implications on capacity building in education. A descriptive survey type was adopted for the study. The instrument used for the study was questionnaire that sought information from teachers. The population for the study consists of all teachers in Akoko Area, out of which a sample of 237 teachers from twelve Schools were randomly Selected. The data collected were analyzed using simple percentage, and chi-square statistics. The study reveals that the exclusion of biology teaching from non-science student’s curriculum has significant relevance on student’s career prospect in tertiary institution. This was also found to have negative impact on the progressive development of non- science students. Based on the findings of this study, that the application of Biology teaching in Senior Secondary Schools has helped students in the time past in handling issues relating to health and hormonal changes during adolescence, as well as for lifelong education which has direct positive implication on Education diversity and knowledge versatility in its application, it therefore means that non- Science Students’ would be deprived of these benefits. It is therefore recommended that an important subject like Biology that has high link with Capacity building of students’ life and their health status should be considered for all. 
			</p>
		<p>
<b>Keywords:</b> Biology exclusion, capacity building, curriculum, and senior secondary school students.   
</p>

