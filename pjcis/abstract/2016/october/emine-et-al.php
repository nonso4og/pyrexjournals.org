			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The effect of graded doses of Garcinia kola seed (G. kola seed) on serum cholesterol concentration and some hematological and liver function indices of wistar albino rats was studied. Different groups of the animals were fed powdered G. kola seed (0 mg, 50 mg and 100 mg/100g body weight, respectively) once a day for 60 days. Results obtained showed that G.kola seed feeding produced in rats a significant decrease (p < 0.05) in serum total cholesterol concentration, and a significant increase (p < 0.05) in white blood cell (WBC) count. The other parameters studied, including haemoglobin (Hb) concentration, percentage packed cell volume (PCV), red blood cell (RBC) count and the biomarkers of liver function such as serum total bilirubin concentration, and activities of serum aspartate aminotransferase (AST), alanine aminotransferase (ALT) and alkaline phosphatase (ALP) were all found to be unaffected by the kola seed feeding. These findings suggest that G.kola seed may have no possible adverse effect on the functions of the liver and may have a beneficial effect as evidenced by its ability to produce significant reduction in serum total cholesterol concentration and a significant increase in the WBC count.

			</p>
