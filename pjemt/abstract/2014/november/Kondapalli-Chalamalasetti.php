			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Deep drawing plays a vital role in the manufacturing process of different parts in various process environments. There is a need to develop different die designs by varying input process parameters for the deep drawing process in an optimum manner. The objective of the present study is to determine the influencing of work piece material on die corner radius in deep drawing process. As, the die corner radius plays a vital role in manufacturing the product in deep drawing process. Lower the die corner radii the part is subjected to tearing effect and higher the die corner radii, wrinkling is observed. The present work deals with 1 mm thick Aluminium, Brass and Stainless steel blank. Static Analysis is carried out in ANSYS for (4mm, 5mm, 6mm, 7mm and 8mm) corner radii of the die.
			</p>

