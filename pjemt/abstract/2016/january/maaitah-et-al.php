			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The aim of this paper is to highlight the effects and obstacles of applying Intelligent Transportation System (ITS) strategies in the directorate general of civil aviation in Kuwait. This work depends on data comprises an interview that conducted and survey questionnaire that investigated more than 300 employees and passengers in the Kuwait International Airport. Additionally, the data gathered was streamlined and analyzed using a computer statistical package (SPSS). The main finding of this paper is that the general advantages of applying intelligent transportation system in aviation. The development that has occurred to the air traffic by enhancing the safety, mobility, people's welfare, safe time and productivity by applying ITS, this enhances the status of the airport as a focal point. Consequently, when it comes to the challenges that face the implementation of ITS, it can be said that the absence of disparate strategic planning in applying the strategic vision for the implementation of Intelligent System can be regarded as an obstacle. In addition, the paper assures that there is significant opportunity to enhance the efficiency, safety, and convenience of airport travelers' access the information through by increasing the use of (ITS) technologies.</p>
			<p><b>Keywords</b>: Intelligent Transportation System (ITS), Kuwait International Airport (KWI), Directorate General of Civil Aviation (DGCA), Self-Service Technology (SST).</p>

