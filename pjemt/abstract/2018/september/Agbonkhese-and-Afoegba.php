			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This research work focused on the design and fabrication of a portable dry groundnut husk peeling machine electrically powered by a 1 H.P electric motor in order to reduce the rigours encountered by the traditional (manual) method of peeling while optimizing the production of good quality groundnut seeds. Peeling is the process of removing the husk stuck to the seeds. The machine was designed, fabricated and tested. The materials for the fabrication were locally sourced which makes it cheap, easily affordable and maintainable. The performance test evaluation shows that the portable machine has a performance capacity of 36.12kg/h with a peeling efficiency of 92.14%. The digester is made up of simple components that can be easily assembled.</p>
		
