			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This research work is aimed at improving the mechanical properties of a mild steel weldment. It was done to understand the problem of poor mechanical properties of the weldment from the thermal effect of the welding process. The problem was addressed by heat treatment, destructive test such as tensile test, hardness test and metallographic examination test. Nine (9) Samples of low carbon steel were examined from a weldment, six (6) sample was subjected to annealing and hardening heat treatment while the other three (3) served as the control sample. The mechanical properties such as tensile strength, hardness and microstructures of the weldment are observed. The results are compared with reference to the heat inputs and heat treatments. The result after the test shows that the annealed sample has high tensile strength, yield strength and ductility; the hardening gives a low yield strength and tensile strength with relatively low ductility, while the control gives moderate tensile strength, yield strength and very low ductility. The hardness result shows that hardening has high hardness, followed by the control and then the annealed. From the microstructure result the annealed has more ferrite than pearlite, big grain size which makes it to be soft and very ductile, the hardening has more pearlite than ferrite, small grain size which make it to have low ductility and high hardness, while the control has moderate pearlite and ferrite, the grain size is moderate, it has reduced ductility and hardness. Hence heat treatment affects the strength, ductility and hardness of the A36 low carbon steel.</p>

