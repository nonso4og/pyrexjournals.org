			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The study of grade intersection requires understanding of the principles of both traffic and highway engineering. The operation of an intersection is influenced by its capacity, queue length and delay, accident potential, vehicle operating characteristics and traffic control. The physical layout of an intersection is defined by its horizontal and vertical alignment, roadway cross-section, surface texture and drainage. The successful integration of these entire factors is required for good design, which must overcome the potential safety and operation conflicts that are inherent when traffic streams interact at intersection. To achieve the said objective for removing congestion and for the smooth flow of traffic, the methodology followed is the traffic survey at the specific intersection grades, previous traffic data's, accident studies, vehicle growth factors, etc. will be analyzed to come out on a result or conclusion from which we can develop the techniques to improve the traffic flow and congestions at the different grade intersections.</p>
		
		<p>
<b>Keywords:</b> Traffic Survey, Accident study, Vehicle growth, Congestion.</p>

