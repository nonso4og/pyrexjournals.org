			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Various scholars and researchers have recognized that the development of the financial sector has productivity and growth-enhancing effects. In this regard, several policies that increase financial deepening have been nurtured with the aim of improving economic growth both in developed and developing countries. Access to financial services in Nigeria is still relatively low. As at 2010, only 36.0 per cent were served by formal financial services, compared to 68.0 per cent in South Africa and 41.0 per cent in Kenya. In addition, about 34.0 per cent of the excluded population had no formal education, while 80.4 per cent reside in rural areas. A large percentage of Nigerian populace is unbanked. Therefore the rate of financial innovation adoption is low. This study sought to investigate the effects financial incentives (Interest rate) on financial innovation adoption in the Nigerian banking sector. The study achieves this objective using data from CBN and NBS from 2005 to 2010. Financial Incentives: the independent variable was captured by Interest Rate, the dependent variables; Financial deepening and financial innovation adoption were measured by Customers and Deposit base. Multivariate regression analysis was used to analyze the secondary data collected. The study found that in Nigeria, financial incentive - interest rates were effective for financial innovation adoption by increasing banks customers' deposit base, but interest rate is not enough to draw more people to the bank (financial penetration), i.e. improving customer base. However, the empirical results also revealed that there is more to financial innovation adoption and penetration other than just monetary instruments (interest rate), because banks have limited power on interest rate movement, which is subject to directives from CBN. The study therefore recommends policies that will encourage the public to have more confidence in the financial system and Deposit money banks. This will encourage more people to patronize financial innovation products and services, participate in economic activities, borrow and invest more, there by bringing quick turnaround to economic and financial development in Nigeria.</p>

<p>
<b>Keywords:</b> Financial Deepening, Economic growth, Behavioral pattern and Deposit money banks.
