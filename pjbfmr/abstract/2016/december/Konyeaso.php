			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Globalization is an inevitable phenomenon in human history that’s been bringing the world closer through the exchange of goods and products, information, knowledge and culture. In the last few decades the pace of global integration has become even faster due to the advancement of technology. Nigeria is not an exception in this globalization process. With the role of providing natural resources, Nigeria has become one of the largest oil producers. The purpose of my study was to find out the kind of impact globalization is having on Nigerian economy, and to suggest better ways of approaching this phenomenon. In order to conduct this research I examined already existing materials that deal with similar topics. I have undertaken a quantitative method of data collection from 1986 to 2013. My data analysis technique was multiple regression analysis. My model has established that there is a positive relation between globalization and economic growth. It is in agreement with my prior expectation. Hence I rejected the null hypothesis and accepted alternative hypothesis. This made me come to the conclusion that Nigerian economy is gaining from globalization mainly due to Foreign Direct Investment (FDI) and trade openness. Since Nigeria has opened up to international trade and foreign investment it has achieved a better economic growth. Thus, for Nigeria to benefit more from globalization we needs to integrate further into international trade by diversifying our exports. A better business environment is also required to attract more foreign investment. ” 

</p>

