			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Dwindling oil prices and global recession have created new challenges and opportunities for oil and gas companies in Rivers state of Nigeria to ride out the storm and emerge stronger. The increasing customer's demand for high quality petroleum products and more flexibility all at lower costs have generated a new trend in the global oil industries. This can be mitigated by effective adoption of various production processes. The paper seeks to address how lean production system can improve the performance of these oil and gas companies. The study specifically identified the type of relationship that exists between cellular production as one of the strategies of lean production and service delivery of these companies. The study adopted correlation research design and research question was presented with deceptive statistics and hypothesis was tested with Pearson Product Moment Correlation. The finding revealed that there is a significant positive relationship between cellular production and service delivery of these companies. The study concludes that lean production can be used to resolve severe organizational performance problems in the oil and gas industry in Rivers State of Nigeria. The paper recommends that managements of these companies are encouraged to increase its resource commitment to staff training so as to develop skills and to update knowledge on lean production; internal customer and supplier relationship should be created in the process of this integrative process.</p>

<p>
<b>Keywords:</b> Lean Production, Cellular Production, Performance, Service Delivery and General System Theory.
