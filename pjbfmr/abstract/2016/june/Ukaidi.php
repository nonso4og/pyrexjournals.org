			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This paper has attempted to offer a succinct literature on entrepreneurship with emphasis on mortality resistance and survival strategy in a turbulent Nigeria business environment. Drawing from a sample  size of one hundred and twenty seven (127) and adopting survey research designs, it was found that only small fraction of the closure occurred in the infancy. A lot more sustained resistance and survival. The paper made far reaching recommendations to increase entrepreneurial success.</p>

<p>
<b>Keywords:</b> Mortality, survival, decline, closure and longevity.
