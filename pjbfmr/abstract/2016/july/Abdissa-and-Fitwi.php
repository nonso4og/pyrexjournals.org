			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		It is of great interest to improve an organization’s service quality by investigating the antecedents of customers’ satisfaction in a dynamic, global business environment, service industries try to compete by providing high-quality customer service via empowering their workforce. Currently both practitioners and academia have shown a growing interest in the concept of employee empowerment. Empowerment has become particularly important for services, aiming to control or enhance service quality and customer satisfaction at the point of service production. Furthermore, empowerment increases job satisfaction and reduces role stress. The main purpose of this study is to investigate the impact of psychological empowerment on employees’ job satisfaction. The research design is a descriptive research. Both primary and secondary data were used in this research. A survey questionnaire with five point Likert scale was a main tool for gathering primary data about psychological empowerment effect on job satisfaction. 123 permanent employees were participated in filling the questionnaires. The researcher used censes sampling method. The data was analyzed by using appropriate statistical tools such as mean, standard deviation correlation, and regression, ANOVA, by using SPSS (statistical   packaging for Social Science). The findings show that Psychological Empowerment has impact on employees’ job satisfaction. All the independent variables are positively and directly related to employees’ job satisfaction particularly in commercial bank of Ethiopia. The relationship between psychological empowerment and employees’ job satisfaction is significant.  From the psychological empowerment meaning and impact are highly and significantly explain employees’ job satisfaction. Therefore, the bank should focus more on meaningfulness and impact to increase employees, motivation, moral and productivity.

</p>

<p>
<b>Keywords:</b> Psychological Empowerment, ANOVA, SPSS, Job Satisfaction.
</p>

