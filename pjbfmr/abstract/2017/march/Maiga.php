			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The Nigerian economy faced numerous challenges which impacted the overall economic activity and has witnessed crises with devastating consequences on the world commodity prices as a result of global economics. This subsequently created structural imbalances occasioned by the collapse of oil prices, which adversely affected the Nation’s revenue. The Study examined the impact of interest rate of economic growth in Nigeria from 1990 to 2013. The result found that the interest rate has a slight impact on growth; however the growth can be improved by lower the interest rate which will increase the investment. As a result of the study was found out that Nigerian authorities should set interest rate policies that will boost the economic growth. Therefore, proper measure should be taken in order to have a more rapid economic growth. 

</p>

