			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This study examines the relationship that exists between value management and economic recession in Nigeria. The study was guided by one research questions and expo facto research design using time series data from the period of 2007-2016. The data used was a secondary data and the data collected was analysed using ordinary least square regression with the aid of Statistical Package for social science. The findings revealed that there is a negative relationship between poor budgeting and GDP. The research also concludes that there is a significant positive relationship between poor budget and unemployment rate in Nigeria. The researcher recommended among others that Nigeria must change the structure of the economy and move away from oil to agriculture, value added entrepreneurship, chemicals, pharmaceutical industry, develop the iron and steel industry, and revamp the machine tools industry for local fabrication of industrial tools. 

</p>

<p>
<b>Keywords:</b> Value Management, Recessed Economy and Value Added Entrepreneurship.
