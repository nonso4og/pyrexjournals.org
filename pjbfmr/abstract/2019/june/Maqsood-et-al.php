			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Housing price forecastability is a big problem and takes attention of many researchers to find out the conclusion to know the causes of price forecastability. This study was conducted to explore the determinants of Housing price forecastability. The main factors was crime rate, share of non-retail enterprise, nitric oxides concentration, number of rooms, distance to employment centres, accessibility to radial highway, tax rate, pupil teachers ratio, proportion of blacks by town and low status of population etc. Multivariate data were used. Data is secondary in nature and is and is collected from the Stat Lib library which is maintained at Carnegie Mellon University. A factor analysis technique used to test the hypotheses. 5 chosen factor, and there corresponding communalities are generally high and the 5 chosen factors explain most of the total data variation (0.842 or 84.2%). 

</p>

