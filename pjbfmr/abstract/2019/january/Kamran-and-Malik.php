			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Purpose: The aim of this study is to analyze the impact of key performance indicator on systematic risk that affects the entire market performance. Methodology: This study analyzed the monthly data of selective industry using the multiple regression analysis. This approach has been applied on 12 listed companies of petroleum sector covering 15 years period, 2005 to 2015. Findings: The regression results of seven independent variables reveals that profitability, operating efficiency, growth, tobin q are significant effect on determinants of systematic risk and rest of the variables liquidity, dividend payout, chin model are against their association with systematic risk. Conclusion: The outcome of this study is helpful for all stakeholders to maximize the return by decreasing the risk factor. This study is helpful for decision making process of all stakeholders of selective industry. 

</p>

<p>
<b>Keywords:</b> Systematic Risk, KPI’s, Petroleum Sector, Pakistan Stock Exchange.
