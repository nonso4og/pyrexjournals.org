			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The objective of the research is to find out the mediating relationship of work family conflict with Work life balance on work demand and home Demand among female doctors in the hospitals of Sialkot and also to find out the reasons of these conflict and their solutions Previous Research shows that the most of the researchers conclude that the women professionals face more work family conflict because they have to do dual responsibilities of their work as well as for their home along with the job requirements. Quantitative technique is used for data collection. For this purpose the questionnaire is distributed to 300 female doctors in two districts of Sialkot.179 questionnaires were received from female doctors of private hospitals and 121 from female doctors of government hospitals. Statistical package for social sciences (SPSS) is used for ANOVA, T test, correlation and regression analysis. Multiple regressions analysis is used for testing of simple hypothesis and Barron and Kenny (1986) model is used for mediation analysis. From the analysis of data it is find out that there is negative significant relationship of work family conflict with dependent variable (WD) and independent variable (WD,HD). Every professional woman has to face dual responsibilities which make cause of work family conflict. Age and marital status also mattered. Women who have kids and who has dependent care issues and who’s doing full time job face more work family conflict. 

</p>

