		  <p>Accepted 4th May, 2018</p>
							
			
	
	
			<br />
			<br />
			
			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Microfinance Institutions (MFIs) are distinct financial institutes as theyfocus on constructinga social business innovation model by adopting innovative strategies to efficiently alleviate poverty. Traditionally their performance was evaluated by financial ratios. The paper goes beyond of traditional methods by using two methodologies Operational Ratio Analysis and Data Envelopment Analysis-DEA approach for comparing the efficiency of Microfinance Institutions-MFIs working in Pakistan (2000-2017). The paper uses an input oriented model with Variable Returns to Scale (VRS) and Constant Returns to Scale (CRS) for determining the relationship between inputs and outputs.The findings reveal that majority of selected MFIs are operationally efficient. However,Orangi Pilot Project (OPP) and Khushali Bank are most efficient among the sampled MFIs. The findings further depict that efficiency of MFIs is sensitive towards the selection of inputs and outputs as variables and the choice of techniques in DEA approach. The paper would assist major investors of industry in understanding the existing scenario of MFIs in Pakistan. 

</p>

<p>
<b>Keywords:</b> Operational ratio analysis; Operational efficiency; DEA, Variable Returns to Scale, Constant Returns to Scale.
</p>

