			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This study examines the factors influencing the customer satisfaction in the Hotel industry in Pakistan. The study covers big three cities of the Punjab Province. We collected the data through a structured questionnaire. We collected the 2193 respondent data. By employing the regression analysis it is found that Reception Department, Food and Beverage Department and Price have significant influence on the customer satisfaction of the hotel Industry. 

</p>

