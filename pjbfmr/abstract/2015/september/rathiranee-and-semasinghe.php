			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Microfinance is considered as an important key to access the micro entrepreneurs to assist them in poverty alleviation, health nutrition, education and self employment opportunity etc. Micro credit is one of the ingredients of microfinance. This study seeks regarding the moderating effects of Family size, education, age and experience on their access of micro credit in rural areas of Sri Lanka. Quantitative method was used for gathering information and stratified sampling method has been used to collect the data from 337 respondents through close ended questionnaires. Data analysis was based on descriptive statistics using various statistical tools by using the SPSS package. Regression analysis and coefficient test were used to identify the moderating effects of Family size, education, age and experience on women entrepreneurs through the access of micro credit. This study revealed that the 84.3 percent of the respondents belong to 18- 55 Age group level. 60.2 percentages have more than three family members. 62.6 percentages have below the G.C.E (A/L) Qualification and 73% have below 5years experience. Based on the findings, it has been concluded that there is no moderating effects of age, family size, education and experience on women entrepreneurs through the access of micro credit. It is suggested that more attention should be given to the training and education facilities other than credit delivery to enhance the entrepreneurship development in the rural areas.
			</p>

<p>
<b>Keywords:</b> Demographic factors, micro credit, rural women empowerment and moderating effect.
