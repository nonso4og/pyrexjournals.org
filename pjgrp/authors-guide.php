References in languages other than English MUST be referred to by an English translation.
</p>
 

<h1 style="font-size:18px; " class="box-header-blue">Tables and Figures</h1>
<p>
All tables and figures must be relevant and necessary; the same data should not be presented in tables and figures, and do not use short tables for information that can be easily presented using text. Tables and figures should be numbered sequentially, for example, Figure 1, Figure 2, Figure 3 and so on.
</p>
 

<h1 style="font-size:18px; " class="box-header-blue">Fees and Charges</h1>
<p>
Authors are required to pay a $400 processing fee. Publication of an article in the Pyrex Journal of Agricultural Research  is not contingent upon the author's ability to pay the charges. Neither is acceptance to pay the processing fee a guarantee that the paper will be accepted for publication. Authors may still request (in advance) that the editorial office waive some of the processing fee under special circumstances. 
	</p>		
