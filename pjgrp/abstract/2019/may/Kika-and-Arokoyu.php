			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Corporate governance of organizational environmental behavior has been largely inadequate to attain desired levels of environmental protection in Nigeria. However, the studies considering the corporate environmental management systems are still few in the literature. The study examined the spatial assessment of corporate environmental management systems (EMSs) of selected companies in Eleme Local Government Area, Rivers State Nigeria. The study made use of Indorama Petrochemical Company Ltd, Port Harcourt Refinery Corporation and Notore Chemical Industries. Three hundred copies of questionnaire were administered to elicit information from the Supervisors and other workers from the selected companies. Descriptive statistics in form of frequencies and percentages were used for the data analysis. Findings showed that the industries were manufacturing and extractive industries. Results revealed that 68.7% of the respondents agreed that they practiced EMS while 63.3% agreed that the commencing of EMS was after 2014 while 36.7% agreed that EMS commenced before 2014. Findings confirmed that more than 90% of the total respondents agreed that all the companies emit or discharge waste into the environment. Majority (68.7%) agreed that pattern or intervals of waste or pollution monitoring was structured at periodic intervals of 7 days while 68.0% of respondents agreed that emission target is set and 32.0% disagreed. Results showed that 93.3% of respondents that all the companies located in the study area were very sensitive to the discharge of waste or emissions into the environment. The study recommended among others that a strict adherence to the established standards be maintained to guide their operations and a periodic audit be also maintained to ensure sustainability of the environment.

			</p>
		<p>
<b>Keywords:</b> Corporate, EMS, Periodic Audit, Sustainability, Eleme LGA.


