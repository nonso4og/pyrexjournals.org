			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Psyllids of the Palearctic genus Camarotoscena Haupt, 1935 (Liviidae: Liviinae) are associated with
poplar trees and distribute in Central and Southern Europe, Afghanistan, Armenia, Azerbaijan, China,
Iran, Iraq, Kirgizstan, Mongolia, Russia, Tajikistan, Turkmenistan, Turkey and Uzbekistan, so far. A
study was conducted to collect and identify the psyllid fauna of the Kurdistan province of Iran during
2012 and 2013. C. trjapitzini Loginova, 1968, previously known only from Armenia, was collected during
the study in Malkeshan Sofla, Sanandaj, the center of Kurdistan province, on Populus sp. This is the
first record of the species from Iran.</p>
