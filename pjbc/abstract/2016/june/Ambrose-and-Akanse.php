			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Exploratory fishery is an important prerequisite for exploitation of commercial fish on an economic basis for every coastal nation. The aim of an exploratory fishery is to carry out a systematic survey of the fishery resources using adequate methods and gear in order to provide data for proper management of fishery resources. Data obtained from exploratory fishery is crucial in tackling several fisheries management issues such as, biomass, distribution, diversity, yield, aggregation and vulnerability to gear as well as long term investment planning. The only and first exploratory fishery survey in Nigeria was conducted in 1964 at depth of 15-50m. The inshore fishing ground (10-30m) depth of Nigeria is overexploited; fishery resources catch is decreasing over the years, price of fish is skyrocketing; above all this part of the sea presently fished is ecologically fragile, being the nursery ground for juvenile of marine fishes. Latent and under- exploited species are abound in deeper sea (50-200m) vulnerable only to natural mortality. Thus, if exploratory fishery is conducted in a virgin fishing ground, it will lead to increase in marine fish production in Nigeria when the data collected is made public to investors and other stakeholders. </p>
