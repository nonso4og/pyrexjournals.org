			<h1 style="font-size:16px; font-weight:600; color:#333333;" class="box-header-blue">Overview and research plan for the introduction of conservation technologies in oceanic shrimp trawling in Nigeria</h1>
			
	    <br />
			
			<h1 style="font-size:15px; font-weight:600; color:#333333;" class="box-header-blue">Eyo Ambrose<sup>1*</sup> and Justina Obienu<sup>2</sup></h1>
			<p><sup>1</sup>Department of Fisheries and Aquatic Environmental Management, University of Uyo, PMB 1017, Uyo, Akwa Ibom State, Nigeria<br />			<sup>2</sup>Department of Fishing Technology, Nigerian Institute for Oceanography and Marine Research, PMB 12729 Victoria Island, Lagos, Nigeria. </p>
