			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		In Nigeria, over 250 licensed industrial shrimp trawlers catch marine shrimps including Penaeus notialis, Parapenaeopsis atlantica, Penaeus monodon and P. kerathurus which are exported to earn foreign currency worth US $50 million annually. Demersal shrimp trawling generates a lot of undersize and juvenile fish bycatch and inadvertently capture the endangered sea turtles as incidental catch to shrimps. The reduction of incidental catch of sea turtles and juvenile fish from the catch in shrimp trawling is regarded as a priority issue in the global efforts to develop more responsible fisheries. The implementation of sustainable technologies in shrimp trawling started with the introduction of Turtle Excluder Devices (TEDs). This is in accordance with the Turtle Conservation Regulation of Fisheries Act of Nigeria (decree) No 71 of September, 1996 (a supplement of the sea fisheries Act (decree) No 71 of 1992. Nigeria was certified in 1998 among other 43 nations, to export all categories and species of shrimps to United States of America based on satisfactory compliance by the operators in the fishing industry. Recently, it was observed that the preserved sea turtles have grown bigger and their current effort is to modify the existing TED and provide a larger existing opening for the turtles to escape. The investigation and introduction of juvenile and thrash fish excluder devices (JTEDs) and other By-catch reduction devices (BRDs) has started. This should guarantee a substantial reduction in the amount of juveniles of commercial fish species (which constitute over 40 % of by catch) in order to guarantee increases in the production (harvest) of food –fish. The socioeconomic implications of reduction of the by-catch in trawl catches and consequently the by-catch trades that are conducted by small scale fishers in the remote villages will also be investigated. The development and introduction of a combination of turtle and juvenile and trash fish excluder devices should be a viable option in the foreseeable future. The lessons learned from the TED experience will be brought to bear and applied in the current JTED project in Nigeria. The introduction of the new technologies through participatory approach will involve all the stakeholders in the industry. The industrial fishermen are expected to play a key role in the design, construction and experimental fishing trials using commercial vessels as well as the trial selection of most suitable/appropriate JTED. Regulatory measures that will facilitate the adoption of the technologies should be up-dated or put in place through bottom-up approach, involving all the stakeholders. Management strategies for monitoring, control and surveillance should be strengthened. Interaction, collaboration and cooperation among member states in the sub-region, especially between Cameroon and Nigeria should be promoted and sustained. </p>
		<p>
<b>Keywords:</b> Marine shrimps, Responsible fisheries, By-catch, Nigeria.</p>

