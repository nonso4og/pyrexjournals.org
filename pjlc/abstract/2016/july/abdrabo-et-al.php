			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Background: The effect of the hormonal changes associated with menopause on plasma lipid levels may play an important role in most cardiac related disorders associated with menopause. 
Objectives: To study the plasma lipid profile in premenopausal and postmenopausal women, and to compare the differences of plasma lipid profile between premenopausal and postmenopausal women. 
Methods: Case control study, carried out in the Aljazeera state during the period from January to March 2015. A total of 60 premenopausal and 60 postmenopausal women were recruited for the study. The subjects have risk factors that may affect the lipid profile were excluded. A 2.5 ml of venous blood was collected after overnight fasting in all the subjects for estimation of plasma levels of total cholesterol, HDL, LDL and triglycerides.
Results: As compared to premenopausal women, mean level of total cholesterol, LDL and triglyceride were significantly higher in postmenopausal women. While the level of HDL was significantly lower in postmenopausal women. (mean±SD) of lipid profile (total cholesterol, HDL, LDL and triglyceride) in premenopausal women, respectively (153.2±22.0, 60.34±4.31, 75.23±21.24, 88.31±20.74) while the (mean±SD) of lipid profile (total cholesterol, HDL, LDL and triglyceride) in postmenopausal women respectively (180.91±28.14, 57.20±8.71, 100.43±26.16, 116.40±41.33)
Interpretation and conclusion: According to the present study, menopause is associated with the altered plasma lipid profile thus, an independent risk factor for developing cardiovascular diseases. Therefore, it is important to consider each and every postmenopausal woman to undergo screening for abnormal lipid profile. In them, specific health education strategies are needed in an order to prevent the emerging cardiovascular diseases.

