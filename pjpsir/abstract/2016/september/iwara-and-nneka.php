			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This work addresses itself to comparative analysis of the allocation of oil-bearing rights in the federations of Nigeria and the United States, and to the central question as to what should be done with the oil-bearing community crisis in Nigeria. Using the descriptive analysis, the study confirms the practical importance of the United States’ model of oil and gas law, with regard to ownership rights, in federal stabilization, governance and development. The study accentuates the argument that the ‘state capture’ and control of natural resources through the use of totalitarian federal might portend a danger of federal disintegration. To this extent, we suggest a revision of the Land Use Decree/Act 1978, enshrined in Article 44(3) of the 1999 Constitution of the Federal Republic of Nigeria, to allow all States of the federation the rights to explore and exploit their natural resources to develop their communities, while paying appropriate tax to the Federal Government of Nigeria.  
			</p>
		<p>
<b>Keywords:</b> Comparative, Federalism, Nigeria, Oil-bearing Community Rights, United States.
