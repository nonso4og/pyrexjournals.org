			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Based on Law no. 46 of 2009 on the Court of Corruption, this court has the authority to examine and decide on corruption cases filed by the KPK and the State Prosecutors and High Offices. According to Article 35 paragraph (1), paragraph (4) of Law no. 46 year 2009 TIPIKOR courts exist only in each provincial capital whose jurisdiction covers the province concerned. The existence of this provision would not want the District Attorney in every district / city should delegate the case to the Corruption Court in the provincial capital. Whereas the area of NTT is an archipelago area where distance between districts with the city of Kupang very far. Based on the results of Phase I (2016) research: the cost required by several State Prosecutors' samples to prosecute 1 (one) case in TIPIKOR Court in Kupang is quite varied, ie Rp.127.534.000, Rp. 294,096,000, Rp. 376,556,000, and Rp. 354,220,000. The costs generally have exceeded the cost limit for the determined prosecution of Rp. 190,000,000, the variation of cost incurred by each State Prosecutor's sample gives an illustration that the more distance from Kupang city, the greater the cost. In addition, the duration of the ongoing trial process has had an effect on the cost. Weather factors, flight delays due to bad weather make the trial schedule chaotic, and costs incurred increases. As a result, the work of witnesses becomes neglected; the Public Prosecutor's job of handling other cases becomes impeded. Based on the matters above, it is necessary to create a sub-district court model and / or IT-based TIPIKOR judiciary to save relatively expensive cost and cut the time available to bring it closer to the quick, cheap, and simple justice principles. This alternative judicial model of TIPIKOR in addition to contributing to the theoretical level of future development of the judicial system is expected to become a model for the mining of the judicial system in other islands provinces in Indonesia.    
			</p>
