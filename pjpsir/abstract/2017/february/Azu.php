			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This study examined the fiscal relationship between the Abia State government and the local governments existing within its jurisdiction. The objective was to find out whether the performance crisis of local governments in Abia State in the area of primary health care delivery is associated with the structure of state-local government fiscal relations. The study adopted the coordinate authority model of intergovernmental relations in explaining the dynamics of interactions between the Abia State government and its local governments. A descriptive survey research design was used to obtain primary data using questionnaires, interviews, researcher's observation and focus group discussions.  Secondary data were obtained from published works and organizational archives. Data was analyzed using simple percentage, frequency tables and Chi-square. Findings showed that the Abia state government has deviated from its monitoring role of local government finances, and assumed absolute control of local government funds, making it difficult for local governments to access their federal allocation and perform their primary duties. The study concludes that the major reason for establishing local government is to extend governance and development to the grass roots. However, until the lingering issues identified in this work are addressed, local government cannot be strategic in promoting any meaningful national development agenda.   
			</p>
		<p>
<b>Keywords:</b> State, Local Government Fiscal Relations, Local Government, Primary Health Care, Absolute control. 
