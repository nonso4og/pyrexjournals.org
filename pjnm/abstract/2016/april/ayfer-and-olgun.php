			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Incidence of diabetes and heart diseases among women is gradually increasing. Both diabetes and cardiovascular diseases usually show minimal symptoms until the development of complications. These symptoms are usually overlooked by women. Nurses have an important role in reducing the risk of these diseases. The aim of this review is to evaluate the effects of diabetes, metabolic syndrome, and female-specific risk factors of heart diseases in women, to overview the preventive health style changes and responsibilities of nurses in this field through emphasizing the importance of risk factors and risk estimation.  
			</p>
		<p>
<b>Keywords:</b> Woman, cardiovascular, risk management, lifestyle changes, risk reduction, nursing.
</p>

