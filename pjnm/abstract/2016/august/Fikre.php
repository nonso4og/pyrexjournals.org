			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The focus of the paper is a systematic literature review which emphasis on factors that can affect  the quality of emergency obstetric care in the health center level in developing countries and in reduction of maternal and child health. Methods: A systematic search of the research which was conducted before 2015, based on the following key words "quality of emergency obstetric care" AND "factors that can affect quality of emergency obstetrics in developing countries .Research papers were analyzed according to component of structure and process. Results: This review leads to the following main conclusions: (1) poor skill of health care provider to manage emergency obstetric, (2) Absence of essential equipment and essential drug, (3) Health care provider shortages. 
			</p>
