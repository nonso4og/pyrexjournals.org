			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The Nigerian tarsand is majorly found in the Western part of Nigeria, the deposits spans a distance of about 120km, the tar deposits occur as heavy oil and manifest as seepages at near surface or on the surface and as impregnated sediments at road cuts, cliff faces and river banks and at break of slopes. Samples were collected at Agbabu, Ilubinrin and Loda where open mining is in progress. The asphaltene content was determined and the concentration of a suite of trace metals, mainly the biophiles were determined. The GC&#8210;FID (Saturates and Aromatic hydrocarbons) were examined. The objective of this study is to determine the genetic relationship of the tarsand. The results indicate that the sample (A1) obtained from the drilled wellbore showed an outstanding difference from others. The seep samples (A2, A3, A4, and A6) irrespective of the locations showed a close relationship as represented by the ternary plot. Samples with significant sand content (5, 7) also showed similarity. It is suggested that since the samples (A2, A3, A4, and A6) were seeped, they could have migrated via structural permeability e.g. faults and fractures, while samples 5, 7 could have migrated via a pore network in the trapless reservoir sandbody. The % Asphaltene versus Co/Ni ratio and Pr/Ph versus Pr+Ph/ (nC17+nC18) plots discriminates the samples into families.

			</p>
		<p>
<b>Keywords:</b> Genetic origins, petroleum geochemistry, tarsand, trace metals.
