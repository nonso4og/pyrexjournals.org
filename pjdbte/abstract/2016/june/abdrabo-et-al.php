			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Background: HbA1C is now formally endorsed in many countries as a diagnostic test for (type 2) diabetes as well as for monitoring, although some debate still continues regarding it is applied for diagnosis. Objective of the Study: The study aimed to measure, HBA1C, FBG and urine chemistry in new diabetic patients. Material and Methods: A case control study was conducted on 50 newly diagnosed type 2 diabetes mellitus patients and the control groups consisted of 50 healthy in Sharig-Elneel Province during January to April 2015. Their HbA1c and fasting blood sugar (FBG) was tested. The association between HbA1c and FBG and also their sensitivity, specificity and predictive values in the detection of abnormal values of each other were determined. Analysis of biochemical parameters was performed by Cobas c111. Fasting blood sugar and HbA1c also compared according to age and BMI in diabetic patients. Statistical analysis was done by student-t–test in addition to chi-square. The P value < 0.05 was considered as significant. Results: The study was conducted on 50 newly diagnosed type 2 diabetes mellitus patients with mean age 44.70 ±12.1 years and the control groups consisted of 50 healthy individuals with mean age 35. 5±12.7 years. The mean age of patients was significantly higher in type 2 diabetic patients compared to controls (p< 0.05). BMI was statistically significant increased in type 2 diabetic patients (27.8±4.9) kg/m2 weight compared to (24.6±3.8) kg/m2 weight in control subjects. Blood sugars FBG mg/dl (205.1±89.3) and Hba1c% mg/dl (10.3±2.5) was significantly higher in type 2 diabetes mellitus patients as compared to control group FBG mg/dl (89.6±10.3) and Hba1c% mg/dl (5.7±.5) p<0.05. With a cut-off value for the diagnosis of DM of 6.5%, HbA1c had a sensitivity of 89.2% and a specificity of 100%, with positive and negative predictive values of 100% and 86%, respectively. Conclusion: Glycated hemoglobin may be a useful measure for diagnosing diabetes and supports a possible cut-off point &#8805;6.5% that is in line with current recommendations. 
			</p>
