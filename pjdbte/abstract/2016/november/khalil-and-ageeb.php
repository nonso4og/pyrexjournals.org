			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Essential fatty acids are polyunsaturated fatty acids (PUFA) which contain more than one double bond. The purpose of this study was to investigate the level of fatty acid composition in plasma of neonates and compare them to their head circumference. There were no records about the levels of these fatty acids in Sudanese people. Blood samples were obtained from the maternal side of the cord during delivery in Khartoum state hospitals. Full and detailed history and examinations were performed in the study groups. Plasma lipids were extracted by Folch method and separated by Gas Liquid Chromatography (GLC). These results were expressed as mean and standard deviations. The differences in mean were compared by Mann-Whitney U test using SPSS for windows version 15. The mean head circumference of our neonates was (29±0.6 cm) range (28-35 cm) which was lower than the standard value (35±1.7 cm).Our study demonstrated a negative correlation between head circumference of neonates with different saturated fatty acids and linoleic acid (r=-0.36,-0.34) (p&#8804;0.05), and positively with other omega -6 and omega-3 fatty acids of neonates (r=0.53) (p&#8804;0.05). Low level of neonatal DHA and high level of LA interfere with our neonatal head circumference, which was lower than the international level.

			</p>
