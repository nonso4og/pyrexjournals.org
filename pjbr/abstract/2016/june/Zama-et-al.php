			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Background: Tuberculosis (TB) poses significant challenges to developing economies as it primarily affects people during their most productive years. This study is intended to find out the prevalence of immune (IgG) antibodies against red cells among TB patients.
Materials and Method: Ninety tuberculosis patients were screened for the presence of IgG autoantibodies against red cells in their plasma. The age range was 18 to 80 years with mean age of 36.80±13.074 years. IgG autoantibodies presence was screened by Ortho Biovue system cassettes (AHG/Coombs) technique. 
Results: The prevalence of IgG autoantibodies among TB patients was 11.1%; (10.0% for those on anti-TB drugs and 1.1% for those not on anti-TB drugs). The Odd Ratio for cohort IgG autoantibodies positive was 0.556. There was no statistical significant difference (P = 0.549) between anti-TB drugs and the prevalence of IgG autoantibodies. Age groups 21-30 and 31-40 years were found to have the highest prevalence of IgG autoantibodies among TB patients. There was however, no statistical significant difference between age groups and the formation of IgG autoantibodies (P = 0.753). The sociodemographic factor also indicates no statistical significant differences (p-values> 0.05). All TB patients with IgG autoantibodies were Hausa tribe. Married, business and illiterates had the highest prevalence (6.7% each), the males were most affected (8.9%) in this study.
Conclusion: The study revealed a high prevalence of IgG autoantibodies among tuberculosis patients with 90% of the prevalence among patients on anti-TB drugs.
</p>
