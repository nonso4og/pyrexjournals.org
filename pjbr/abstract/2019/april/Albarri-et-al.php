	    <br />
			
			<h1 style="font-size:15px; font-weight:600; color:#333333;" class="box-header-blue">Osman Albarri<sup>a*</sup>, Isil Var<sup>b</sup>, Basak Bedir<sup>c</sup>, Manaf Al-Matar<sup>a</sup> and Fatih Köksal<sup>c</sup></h1>
