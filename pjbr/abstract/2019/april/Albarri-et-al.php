			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Iron is an essential element for Escherichia coli growth and survival in the host and the external environment, but its availability is generally low due to the poor solubility of its ferric form in aqueous environments and the presence of iron-withholding proteins in the host. Most E. coli can enhance access to iron by excreting siderophores such as enterobactin, which has a very strong affinity for Fe3+. A smaller proportion of isolates appeared to generate up to 3 additional siderophores associated with pathogenesis; aerobactin, salmochelin, and yersiniabactin.  The aim of this study was to compare the presence of siderophore genes in E. coli strains isolated from clinical and food samples. Sixty five and thirty five of E. coli strains were isolated from the clinical and food samples, respectively. The prevalence of the siderophore-related genes, which was determined in the E. coli strains by using PCR method, was as follow: enterobactin receptor (Fep A), 18%; enterobactin biosynthesis (Ent A), 18 % ; yersiniabactin receptor (Fyu A), 17%; heme receptor (Chu A) 16%; aerobactin receptor (iut A),12%; aerobactin biosynthesis (iuc A), 15%; and  salmochelin receptor (iro N), 3%, and iron-responsive element (ire A), 1%. The siderophore-related genes are considered as main factors in the growth and survival of the clinical strains of E. coli and adaptation to their low-iron environment. Our results strongly supported that and found the prevalence of siderophore genes is significantly higher among clinical isolates than among food isolates.  
</p>
