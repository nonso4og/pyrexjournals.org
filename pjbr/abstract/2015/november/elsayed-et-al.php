			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This study aimed to investigate the toxic effects of gasoline on antioxidant status of brain tissue of mice. On the other hand, studying the protective and the ameliorative role of green tea extract on these toxic effects of gasoline. Green tea extract was chosen as an antioxidant and antitoxicity natural products. CD1 mice were taken as an experimental model. Mice were exposed to gasoline vapor 2hours/day for 3 weeks in inhalation chamber. Green tea extract was provided to mice as their sole source of drinking water, these were taken before starting inhalation with one week and along the time of experiment till sacrificing the animals.
Oxidative stress markers were done in this study to determine the oxidative stress of gasoline, on the other hand to examine the protective and the ameliorative role of green tea.
The results of this study were concluded as:
1. Marked increase in lipid and protein oxidation levels in brain tissue by gasoline inhalation with highly protective effects of green tea on this oxidative stress of gasoline.
2. Destruction for thiol compounds, which acted as non-enzymatic antioxidants, with gasoline, these returned to the normal levels with green tea.
3. Significant reduction in the activities of antioxidant enzymes, which damaged by gasoline, some of them were improved with green tea and the others did not affect. Catalase and GPX were improved with green tea, but G-S-T and SOD did not affect.

			</p>
