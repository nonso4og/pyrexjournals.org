			<br />
			<br />
			
			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Malaria is a major cause of death in tropical countries, it caused by different species of plasmodium, falciparum, vivax, malaria and ovale, even malaria diagnosis measures are setting properly, and miss-diagnosed cases are still causing death and major health complications. Causative agents for miss-diagnosis case could be due to protocol used, which include staining methods, trained personal and occupational work areas. So this study aimed to evaluate laboratory work for testing malaria infection by blood films, whether thick or thin, tools involved and so on. 50 members of laboratory workers, at Kordofan state were evaluated though their educational levels, training, protocol of laboratory work. Data obtained was analyzed using the statistical package of social science program. Most of laboratory workers were well educated with increased experience, conducting optimal malaria diagnostic tools, such as standard staining preparation was followed by 3%, timing of staining done by 83% and performing of thin and thick blood films were found among 89% of personal. Level of education and type of training involved workers presented with influence on results obtained, as when they were compared significant difference was obtained, p value 0.000 and 0.032 respectively, but experience duration did not affect the results obtained. So it recommended to follow the ideal protocol of routine malaria testing steps and it should include raise the qualification of personal and provided excellent tools for such reason.  
</p>
		<p>
<b>Keywords:</b> Plasmodium, protocol, miss-diagnosis.</p>

