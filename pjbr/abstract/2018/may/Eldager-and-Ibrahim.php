			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Background: Complications of tonsillitis are rare, and usually only occur due to untreated bacterial infection, One of these complications is a Rheumatic heart disease. Objectives: The aim of this study was to assess the level of troponin-I as a cardiac marker for rheumatic heart diseases among Sudanese Patients with recurrent Tonsillitis. Methods: a Cross sectional case-control study was conducted during the period from November to December 2017, forty samples from diagnosed patients with recurrent tonsillitis (admitted to Ear-Nose and Throat hospital in Khartoum state) as cases and forty samples from healthy individuals as controls, the level of troponin-I was measured by using an automated enzyme immunoassay system (TOSOH), Data analysis was carried out by SPSS version 16. Results: There was a significant  increase of serum troponin-I level in tonsillitis patients with p-value = 0.036 when compared to healthy individuals (The (Mean±SD) was (0.126 ± 0.059)  and (0.070 ± 0.004 U/L) in tonsillitis patients and healthy individuals respectively), Also there was a positive correlation between level of troponin-I and duration time of disease with (R= 0.437, p-value= 0.005), but there was no correlation between level of troponin-I and age with (R= 0.288, p-value= 0.071). Conclusion: the level of troponin-I is increased in tonsillitis patients and the increasing level is positively correlated with duration of disease.  
</p>
		<p>
<b>Keywords:</b> Tonsillitis, Rheumatic heart diseases, Troponin-I and Sudanese.</p>

