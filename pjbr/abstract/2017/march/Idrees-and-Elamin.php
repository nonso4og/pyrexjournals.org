			<h1 style="font-size:15px; font-weight:600; color:#333333;" class="box-header-blue">Evaluation of the Performance of Preservation Methods in the Detection of <i>Schistosoma Haematobium Ova</i> in Urine Samples</h1>
			
	    <br />
			
			<h1 style="font-size:15px; font-weight:600; color:#333333;" class="box-header-blue">Safaa Elsheikh Idrees* and Elamin Abdelkareem Elamin</h1>
			<p>Faculty of Medical Laboratory Sciences, AL-Neelain University, Sudan.</p>
			<p>Corresponding Author E-mail: safaa0600@hotmail.com</p>

		  <p>Accepted 6th March, 2017</p>
							
			
	
	
			<br />
			<br />
			
			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The quality of formalin, boric acid, domestic bleach and carbol fuchsin were investigated as preservatives of Schistosoma haematobium ova for three weeks. Urine specimens obtained from patients included in a survey conducted in the Maio area in Khartoum the urine specimen was divided into 4 aliquots within 12h of the collection into the four preservatives mentioned above and were processed after one week, two week and three week of wet preparation of 14 positive specimens (a total of 217 processed samples) after concentration by centrifugation. We found that all fixatives give good morphological characteristics for S. Haematobium with some morphological changes, and crystal formation with formalin, boric acid and domestic bleach which appear after the first week. Formalin is aldehyde base fixative, domestic bleach is oxidizing agents based fixative, both have action of cross linking in preservation of samples which may cause these morphological changes. Carbol fuchsin gave the same morphological appearance during the three weeks. According to this result, we conclude that  non-cross linking alcohol based fixatives such as carbol fuchsin gave good preservation for the  morphology of S. Haematobium ova as the morphological characters did not change during preservation time.  
</p>
