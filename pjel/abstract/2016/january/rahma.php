			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This study investigated the use of communication strategies (CSs) by 60 students of English as a foreign language (EFL) at Ibri College of Technology in Oman. The study was qualitative in nature. Data elicited from audio recordings of picture description task and semi-structured interview have been categorized according to a taxonomy compiled from various pre-existing taxonomies of such strategies. The results obtained show considerable variation across the range of CSs used by Omani students. Further investigation indicated learners’ levels of proficiency influenced the use of communication strategies, with higher proficiency learners making more use of approximation and circumlocution, while lower proficiency learners used more avoidance strategies and L1-based strategies.


  
			</p>
		<p>
<b>Keywords:</b> Communication strategies, EFL, English language proficiency, strategic competence.
</p>

