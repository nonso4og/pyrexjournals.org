			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The purpose of this study was to verify the efficacy of the Language Laboratory in the teaching and learning of some phonemes of English language perceived as difficult. Based on the result of sixty four (64) students from the Federal College of Education Yola, a t-test statistics revealed a significant relationship between the use of the laboratory and performance. In view of the significant relationship established, the paper posited that progress in speech articulation, especially in Nigeria where English is taught, learned and spoken as a second language and where some sounds of English do not exist in the speaker’s First Language (L1), is predicated on the ability of the teachers and learners to make effective use of the language Laboratory. In conclusion, the educational implications of the findings were discussed.
  
			</p>
