			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Composite soil sampling method for the collection of soil samples between the depth of 0-15cm was adapted. The soil samples were collected from sampled plots under pearl millet sole (major cropping) and tested for some physical and chemical properties. The result reveals that mean temperature, rainfall, relative humidity and average pH were optimum for pearl millet cultivation. Soil properties including average clay (8.71%), AWHC (1.17cm), P (14.57ppm) and CEC (1.583me/100g) were found under pearl millet sole cropping to be low. The 'suitability rating' of temperature, rainfall, relative humidity and pH were found to be suitable (S1), nitrogen and organic carbon were found to be marginally suitable (S3); available phosphorus, potassium, calcium, magnesium and CEC were temporary not suitable (N1) for the crop in the area. Analysis of variation (ANOVA) revealed that there is no variation among the soil samples. Therefore, the findings of this study indicate that, pearl millet crop cultivation has effect on soil which led to low suitability. Mixed cropping systems, integration of organic and inorganic fertilizers and improvement in traditional fertility management such as tillage and early clearing should be maintained to increase pearl millet production to meet the people's demand for sustainable development.</p>
		<p>
<b>Keywords:</b> Soil, pearl millet, katsina.</p>

