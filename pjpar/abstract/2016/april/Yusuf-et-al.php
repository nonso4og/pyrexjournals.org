			<br />
			<br />
			
			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The study was conducted to determine the effects of substituting maize with Prosopis africana pulp (PAP) on growth and economic performance of finishing broilers. One hundred and fifty day-old chicks were used for the study. The chicks were brooded for four weeks during which they were fed commercial starter diet. At four weeks of age, the chicks were randomly allotted to five dietary treatments in three replicate (ie thirty per treatment and ten per replicate). The PAP was included at 0%, 5%, 10%, and 20% levels in diet 1 (control 2, 3, 4 and 5 respectively, replacing maize as an energy source. The parameter considered in the study were feed intake, rate of gain, feed conversion ratio, protein efficiency ratio and cost benefit analysis. Highest feed intake (130.08g/bird/day) and weight gain (56.30 g/bird/day) were recorded in 0% level of replacement. Feed conversion ratio was similar (P&gt;0.05) among treatment 1, 2 and 4 which were significantly better (P&lt;0.05) than 20% level of replacement (4.05). Protein efficiency ratio was similar across the treatment groups. The feed cost per kg decreased with increase in level of PAP in the diets (ranging from N 62.17 in diet 1 to N 54.39 in diet 5). However, feed per kg weight gain was lower in 5% level of replacement than the other treatments. Results from this study showed that feed consumption and body weight gain was adversely affected by increasing levels of PAP across the treatment groups. Therefore, inclusion of PAP should not exceed 5% of the diets of broiler chickens.</p>
		<p>
<b>Keywords:</b> Prosopis pulp, Broiler, Performance, Cost benefit.</p>

