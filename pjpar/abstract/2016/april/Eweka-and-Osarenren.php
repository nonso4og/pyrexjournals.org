			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The study looked out the profitability and the relationship between cost of input used and income realized of raising broilers under the battery cage and deep litter system of poultry production. The data used in the study were obtained from a cross-sectional survey of broiler farmers in EdoState from October–December, 2014. A multi-stage sampling process was used to select the respondents for this study. The data collected were analyzed using descriptive statistics, profitability ratios and ordinary least square (OLS) multiple regression model. The Gross Margin analysis gave a value of N2,422.24 and a Net Farm Income (NFI) of N2,412.40 per bird for battery cage system while the deep litter system had a gross margin of N1,601.77 and NFI of N1,593.80 per bird. The profitability ratios showed Rate of Return on Investment (RRI)of about 92%, Return on Labour(RL) of N18.03, Return on Feed (RF)ofN144.22 and Return Per Naira Invested (RNI) ofN0.91 for the battery cage system as against RRI (71%), RL (N30.28), RF (N117.95) and RNI (N0.71) for the deep litter system. This shows that both systems were profitable and viable in the study area.Only three variables in the regression model were found to be statistically significant (P<0.05), these were feed cost, electricity, and purchase cost of day old chick for both the battery cage and deep litter systems. Feed cost was the major determinants of revenue accruing to the farmers. Accounting for about 53%.It was thereforeconcluded that farmers should be enlightened on the relative profitability/viability of broiler production in the study area, as a guide to future investment in the enterprise and are advised to compound their own feed as this will help to reduce the cost of feeds which account for over 70% of the total cost of raising broilers from day old chick to point of sale. Lastly the electricity service provider should charge customer according to their consumption.</p>
		<p>
<b>Keywords:</b> Battery cage, Cost, Deep litter, Net Farm Income, Profitability, feed cost, electricity.</p>

