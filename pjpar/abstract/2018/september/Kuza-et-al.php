			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		A survey was conducted to assess the impacts of National Fadama III Development Project (NFDP III) on income generation of beneficiaries in Nasarawa State, Nigeria. The population of this study consisted of all beneficiaries of National Fadama III Development Project in the study area. Two hundred and twenty (220) respondents were selected as sample size using multi-stage, stratified and simple random sampling techniques. Primary data were collected using a structured questionnaire. Descriptive statistics and inferential statistics were used for data analysis. Results showed that the mean annual farm income after the NFDP III intervention was N1,013,022.73 against before intervention N788,636.36 and a significant difference of 13.65 at p. < 0.01. The mean annual non-farm income after the intervention was N639,681.82 against before National Fadama III Development Project N482,727.27 and a significant difference of 14.29 at  p < 0.01. There was a positive difference after National Fadama III Development Project intervention among the beneficiaries of National Fadama III Development Project on income generation for rural and agricultural development in the study area. The study recommended that government in collaboration with financial institutions should organized training for rural farmers on credit acquisition, financial management, utilization for development projects and more credit facilities should be provided to rural cooperative groups.</p>
		<p>
<b>Keywords:</b> Fadama, project, intervention, income, generation, beneficiaries.</p>

