			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The common cutworm moth, <i>Spodoptera litura</i> (Lepidoptera: Noctuidae) is a major agro-forestry pest worldwide. This pest has strong insecticide resistance and thus environmental friendly management methods are imperative to control this pest. In the present study, the pre-copulatory mate choice in relation to age at mating, virginity and body size were investigated and discussed in <i>S. litura</i>. Results show males significantly preferred young females to middle aged or old females for copulation but did not appear to demonstrate any preference between middle aged and old females. However, females significantly preferred middle aged males to young or old males for mating but did not appear to have any preference between young and old males. When offered virgin and mated potential partners, the male selector significantly preferred virgin to mated females while female selectors did not have any preference over selectees’ mating history. Both males and females significantly preferred heavy mates for mating. These data provided useful information for the control of <i>S. litura<i> by using environmental friendly management methods, such as the sterile insect technique.</p>
		<p>
<b>Keywords:</b> <i>Spodoptera litura</i>, tobacco cutworm, mate choice, pest control.</p>

