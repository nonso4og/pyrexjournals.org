			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Horse racing is very popular with the people. Out of 13 horses 12 horses were performed in races in two groups- large and small horses. Distinguished breeds were thoroughbred, barb, saddlebred, standardbred and some crossbreeds. The most common color was buckskin. Minimum 50 thousand people were present in that program. On the basis of profit the horse is doubly profitable animals than cows. By rearing from colt then after mature its price is higher. ‘Shongholshi Kadikhol Math’ was the venue for horse racing. Most of the people who enjoyed this race were maximum mid-aged. There were little old-aged and children also. In Saidpur upazila this program was held in the year 2010, 2011, 2013, 2014 and 2015 gradually. Union chairman was the chief guest in this horse race. Due to proper care these racing horses’s longevity was shown higher than wild one. Normally, all horses bought from India. Kurigram and Mymensingh in Bangladesh are the main horse rearing place. If it’s possible to train-up horses by renowned horse expert then this horse rearing and racing culture could be a good source of income. All prizes were really remarkable for the horse race winner. Need to use horses in races after four years and not use pregnant animal. Always need to check its health especially legs and hooves. Due to grazing in open area sometimes it affected cholera and in winter season simply fever, cough and all time acidity is common. But the diseases are not fatal for them. Simple medication is sufficient for this. Only diarrhea and worm infestation are major two problems in horses. Between two worms infestation is very serious. That time horse is shown very excitation and rubbing its body on ground. Allopathic treatment by a vet is better in that case. Observed racing horses male and female sex ratio was 10:3. It’s very important to preserve our culture as well as the rearing of horses.
			</p>
