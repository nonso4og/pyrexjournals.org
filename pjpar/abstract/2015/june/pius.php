			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The rising poverty incidence amidst “economic prosperity” and the restiveness in some part of Nigeria which has been repeatedly tied to poverty has agitated the minds of scholars. Policy prescriptions on tackling poverty are not in short supply, but often of the majority of the labor force (rural household) which is the worst hit by poverty, appear to hold much prospect in the fight against poverty. Understanding the determinants of criticizing as palliative measures with sustainability in doubt, increasing the earning capacity their earning capacity is therefore instructive. Using the Jacob Mincer model in its elaborate form, a cross sectional data set obtained from six hundred and fifty seven farmers were applied to an ordered probit model. Among others, it was revealed that age, years of schooling and experience have the likelihood of impacting positively on the earning capacity of labor. Though the rate of return on schooling was low, perhaps given the nature of occupation and the type of education given, it was however, recommended that agric business education on staggered modules be implemented in the study area. 
			</p>
