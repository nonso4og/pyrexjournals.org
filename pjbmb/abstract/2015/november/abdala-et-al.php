			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Diabetes mellitus affects most organs in the body. The manifestations of these effects are generally detected by changes in their functions. Retinopathy generally occurs early in the course of diabetes and, even though no other symptoms are present. It may progress to threaten vision. Therefore, because of potential sequelae to visual function that are preventable, early detection of diabetic retinopathy with an assessment of its severity is needed to prevent loss of vision. The aim of this study is to investigate whether diabetic retinopathy, already associated with microvascular alteration, ischaemia and endothelial dysfunction was also characterized by an abnormal coagulation profile.
Methods: this is a Case-control study in which forty Sudanese type 2 diabetic patients with different degrees of diabetic retinopathy and ten healthy control subjects were included in this study. In all patients, retinopathy was detected by direct opthalmoscopic examination and by using slit lamp biomicroscopy. In both patients and the control group prothrombin time (PT), activated partial thromboplastin time (APTT) and factor VII coagulant activity (VII:c) were done.
Results: Factor VII: c levels were higher in diabetic patients than the control group (p=0.03). There was a significant correlation between factor VII: c level and diabetic retinopathy (p=0.019). Factor VII: c was higher in diabetic patients with retinopathy especially in patients with proliferative retinopathy. PT mean values in diabetic patients were shorter than in the control group (p<0.01). There was a highly significant correlation between PT and diabetic retinopathy (p =0.002). There was no significant correlation between APTT and diabetic retinopathy (p=0.98).
Conclusions: These findings pointed out the presence of a hypercoagulable state in patients with type 2 diabetes with diabetic retinopathy demonstrated by increased factor VII: c level and short PT.

  
			</p>
