			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Our study used a cryo-injury model to induce myocardial infarction in adult male rats by using liquid nitrogen (- 190 Co) and investigate the physiological changes during acute and chronic M.I through checking serum blood biomarker Myeloperoxidase. And doing scoring between the biomarker and the histopathological changes in rat myocardium in different period time. We used a total of 35 adult male rats and divided them into subgroups as following:
-	Acute M.I groups (4/hours, 8/hours and 24/hours) (15 rats).
-	Chronic M.I (7/days, 14/days and 28/days) (15 rats).
-      Control groups (healthy) (5 rats).
On the other hand, the clinical study investigated the physiological changes during AM.I and chronic M.I through checking serum blood biomarker Myeloperoxidase. We used a total of 35 adult male patients complains from M.I and divided them into subgroups as follows:
-	Acute M.I groups (4/hours, 8/hours and 24/hours) (15 humans).
-	Chronic M.I (7/days, 14/days and 28/days) (15 humans).
-	Control groups (healthy) (5 humans). From these experimental and clinical studies, the following results were observed: 
1-	Increased the concentration levels of Myeloperoxidase (MPO) in all study groups in experimental and clinical and acquired this elevated highly, significant (p<0.01) at 4/hours and reach the peak concentration at 8-24 hours of AM.I and persist significant  elevated till 28/days of chronic M.I.
2-	Histopathological Changes: The histopathological changes during acute and chronic M.I reflect the physiological changes in concentration of Myeloperoxidase. In this result showed highly significant (p<0.01) relation between histopathological changes during acute and chronic M.I and serum level of Myeloperoxidase.

  
			</p>
