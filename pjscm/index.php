			<h1 style="font-size:20px; " class="box-header-blue">About | PJSCM</h1>
			<br />
			<p>
			<b>  Pyrex Journal Of Supply Chain Management </b>is an open access academic refereed journal published monthly by Pyrex Journals.
			</p>
			
		  <p>
			The demand for intercontinental freight transportation has increased dramatically over the past few decades. However, the corresponding increase in the supply of products/orders has occurred without too much consideration to the eco-efficiency, sustainability, and safety of the transport. Looking ahead to 2030 and beyond, E-commerce logistics will be under ever increasing pressure due to the rising global trade volumes and the customers’ expectation for fast and safe delivery. In order to meet these pressures, shipping providers, e-commerce platform, as well as supply chain researchers have to develop new technological and management solutions. A further challenge, which is gaining increasing importance in the industry and society, is to consider transport’s service impact on the environment. This journal is devoted to publishing recent original and significant research dealing with a range of emerging issues in logistics and supply chain management. Typical topics include, but are not limited to, the use of analytical methods, computational tools, optimization approaches, numerical analysis, decision support systems, empirical research, theoretical conceptualization, and case studies to address significant theoretical and practical issues in the following areas:<br /><br />

<b>*Design of eco-efficient transportation systems.</b><br />
<b>*Scenario analysis and planning for e-commerce logistics.</b><br />
<b>*Reference models for maritime logistics and port management.</b><br />
<b>*Real-time control and monitoring of inland waterways transport.</b><br />
<b>*Decision support systems in logistics networks.</b><br />
<b>*Performance analysis of multimodal transport and logistics.</b><br />
<b>*Routing, allocation and fleet scheduling.</b><br />
<b>*Assignment and transportation operations.</b><br />
<b>*Resource allocation and stockyard management.</b><br />
<b>*Modelling and analysis for maritime freight systems.</b><br />
<b>*Effective planning for port management, competition and pricing.</b><br /><br />

All submissions will be judged for the appropriateness to the Journal scope and novelty of the

research contributions.			</p>
 

