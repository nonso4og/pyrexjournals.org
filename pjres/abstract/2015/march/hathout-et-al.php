			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Background: The employment rate of mothers has increased worldwide in recent years. So, there is concern about possible effects of work related risk factors and pregnancy outcome in working women.
Objectives: To identify the effect of work during pregnancy on pregnancy outcome.
Subjects & methodology: 500 pregnant women who were visiting the antenatal care clinic at Beni-Suef university hospital were our target group. They were interviewed using predesigned questionnaire, Physical activity questionnaire, Job Stress Questionnaire, Workplace Stress Survey and Body mass index (BMI). Statistical analysis performed using SPSS version 21.
Results:  Perinatal death was significantly higher among working women (odds ratio = 1.9, CI= 1.1-3.8).  Among studied working conditions, working more than 40 hours/ week was significantly associated with high rates of preterm delivery (Odds Ratio= 0.13, CI= 0.05-0.3) and small gestational age (SGA) was significantly increased with high work stress (P= 0.03).
Conclusion: Among the studied work conditions for working women working more than 40 hours/week, social stress index were found to be a significant risk factor for adverse pregnancy outcomes,. Other factors as shift work, work categories, and physical activity score showed no significant association with pregnancy outcomes. Therefore, as long as the health of the pregnant woman permits her to continue work during pregnancy that is not stressful or overloading for her, no adverse effect on pregnancy outcomes is expected. 
 
			</p>

<p>
<b>Keywords:</b> Pregnancy outcome, Shift work, small for gestational age, SGA, Perinatal death, preterm.
