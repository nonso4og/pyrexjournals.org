			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This research sought out the social and economic determinants of fertility in Udi area of Enugu State, Nigeria. It utilized survey design to collect data from a sample of 240 women spread across the five districts of the study area. Analysis was done using frequencies and percentage values displayed in tabular forms and subsequently described and discussed. The study found that socio-economic factors as age at marriage, duration of marriage, marriage type and status, education and occupation all affected fertility differentially. It was found that a woman’s long marriage duration and early age at marriage increases her fertility level. It was equally found that socio-economic status as proxy by education and occupation maintains an inverse relationship with fertility level in the study area. It is recommended a high level emphasis on education of women and their active participation in labour force as this will go a long way in lowering their fertility level and bringing population growth to reasonable and convenient level for the economy.</p>

<p>
<b>Keywords:</b> Social, Economic, Determinants, Fertility, Udi Area.</p>

