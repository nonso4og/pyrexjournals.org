			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Nigerian cities are witnessing high rate of environmental deterioration and are rated among urban areas with the lowest livability index in the world. It is estimated that between 20 percent and 30 percent of the urban population enjoy decent urban life in the country.  This study was carried with the use of questionnaire to ascertain the level of environmental problems due to urbanization in Ado-Ekiti, Ekiti state. Analysis was carried out using Microsoft excel package. It was discovered that urbanization in Ado-Ekiti had resulted into urban slums, sprawl, decay of the inner core, and flooding of the city.
 
			</p>

<p>
<b>Keywords:</b> Urbanization, Environmental problems, Urban slum, Urban sprawl, inner city, flooding.
