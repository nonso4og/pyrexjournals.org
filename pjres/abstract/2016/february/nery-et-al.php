			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Global expenditures on pesticides grow continuously. Brazil is the world's largest consumer of pesticides responding in Latin America with 86% of the products. In 2011, the State with most consumed pesticides in Brazil was Sao Paulo - 346,079.2 ton. Brazil is a country with 5.569 municipalities in 27 units of the federation. Studies show that consumption of pesticides per capita in Brazil is about 5 liters per year. São Paulo is the most populous State in Brazil with 42,000,000 inhabitants. The Epidemiological Surveillance Center of the State Health Secretariat has structured an observatory to provide information about the pesticides consumption, characteristics of substances and main health grievances, enabling descriptive studies and thematic maps of deaths related to consume or exposure to pesticides for different regions of the State of São Paulo. Maps with the consumption of pesticide were constructed in order to contribute to subsequent analysis by teams of health services in the State of Sao Paulo. Preliminary analysis of the information showed an increased consumption and use of pesticides without increase in the cultivated area. Also revealed that the grievances: hospitalizations, cases of cancer, poisoning can be analyzed for each pesticide in each municipality. Shows graphics of poisonings reported in the Brazilian Health Surveillance System (SINAN) with the hospitalizations by poisoning and the diagnosis of the epidemiological profile of pesticide poisoning in the State of São Paulo. The construction of thematic maps serve as guides for initial actions in health surveillance of the population exposed or potentially exposed to pesticides.
 
			</p>

