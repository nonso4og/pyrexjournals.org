			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The stem of Adenia cissanpeloides known as Nkochi Ngwu or Uturu among the Igbos was studied to elucidate the structure of the pharmaceutical active ingredients present in the stem extract of the plant. Thin layer chromatography showed a spot with Rf values of 0.79. The extract was subjected to spectroscopic analysis using FTIR, UV-visible, H1 NMR C13 NMR, GCMS and the structure elucidated was 2-methyl-5-nonanone. Antibacterial and Antifungal Activities of the pure extract using 10 bacterial species, gram positive and gram negative such as S. aureus, E.coli, Streptococcus species, Proteus vulgaris, Enterobacter earogenes, Bacillus species, S.albus and fungal cultures such as Aspergilles flavius, Aspergillus niger and candida albican showed that the chloroform-methanol extract had zones of inhibitions ranging from 12 to 24 respectively. These antimicrobial results were compared with that of some standard antibiotics and it was discovered that the fraction was less potent than the standard antibiotics.        
			</p>

<p>
<b>Keywords:</b> Stem, Chloroform-methanol Extract, Structural Elucidation, anti-Microbial.
