			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Commiphora swynertonii exudate is used by people in Central and Northern Tanzania for treatment of various ailments, including upper respiratory infection and urinary tract infections, as well as anthelminthic and insecticide. Its antimicrobial and acaricidal activities have been demonstrated, but its toxicity has not been studied. The aim of the study was to determine its toxicity using brine shrimp test and oral administration of various doses of the exudates. Brine shrimp nauplii were subjected to various concentrations of the exudates in simulated sea water and by noting the mortalities against each concentration, the median lethal concentration (LC50) was determined using Fig P computer program. As for acute toxicity test, mice were administered with various doses of the exudates by gastric intubation and observed for signs of physical and behavioral changes as compared to mice which were given a placebo. After a 14-day observation period, the mice were sacrificed, and their internal vital organs (heart, liver, lungs, kidneys and spleen) checked/examined for weights / histological changes as compared to the mice which were given placebo. The Brine Shrimp test exhibited moderate toxicity of LC50=15.30 &#956;g/mL. In Acute Toxicity test, no changes in mice were observed at doses below 2000 mg/kg body weight. Some behavioral and physical changes were observed at 3000 mg/kg and some mice died at 4000 mg/kg. The mouse which was dosed 5000 mg/kg died after 3 hours. The LD50 was 3400mg/kg. Histological analysis revealed no significant changes below 2000mg/kg doses. But at 3000 mg/kg and beyond, major changes, including organ degeneration, vasocongestion and behavioral changes began to manifest. Liver and lungs were severely affected at 4000 mg/kg dose and pneumonitis was advanced as the cause of death for the mice that died. We conclude that at doses below 2000 mg /kg, CS exudate is relatively safe. We also recommend further studies to be carried out on CS exudates for anticancer activity.        
			</p>

