			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Background: Hepatotoxicity is a potential complication from the usage of various illicit drugs, but information on this scare in the medical literature
Objective: The aim of this study was to study the assessment of Alanine Aminotransferase (ALT), Aspartate Aminotransferase (AST) and Alkaline Phosphatase (ALP) in chronic Marijuana abusers.
Methodology: This is a case control study, conducted in Khartoum State at Alribat Hospital, during May to August 2015.A 120 blood sample was collected, 60 samples of abuse people as case study, 60 samples as control group. The age and sex was matched, which ranged between 18-60 years. Plasma concentration of ALT, AST and ALP were estimated using PS MINDRAY full automation 380.
Results: (The means SD) of plasma ALT, AST, and ALP in marijuana abuse people respectively, were (31.1±17.5), (26.9±8.2), (77.1±32.8) IU/ml while in the control group, respectively were (59.7±10.5), (63.7±12.4), (169.4±14.2).
Conclusion: This study concludes that chronic abuse of marijuana cause liver hepatotoxicity, because all liver enzymes were highly significant increase when compared with the control group.

			</p>
