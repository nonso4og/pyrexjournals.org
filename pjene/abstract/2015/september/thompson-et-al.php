			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Human activity and globalization have increased over the last few decades, consequently, the introduction and spread of species from one place to another has also increased. Emerging species have greatly influenced our environment as well as native species. This study aims at evaluating plant characteristics of non-native species that makes them invasive, it reveals new invasive species and elucidates the hazards imposed on human health and economic loss in the metropolitan areas of Akwa Ibom State. This research also aims at making special mention of Hydrangea species – a new invading species as well as other plants that pose environmental problems in this area. This study was conducted in three metropolitan areas of Akwa Ibom State between June 2013 and January 2015. These areas are characterized by diverse plant types, biologically stressed conditions and polluted sites. However, wide portions of land often utilized for agricultural purposes have been affected as a result of industrialization and life developments that have brought about the introduction of new species mainly for ornamental purposes. Three areas were studied (based on their high degree of human activity). Species at these sites were identified; plant characteristics were investigated in relation to the prevailing conditions. Influences of species identified to be non-native were investigated in relation to the natives; this was in order to ascertain which species were invasive. Photographs were taken. This study, therefore, evaluated the potential hazards of these species on human health, agriculture and economic loss and therefore proffers possible solutions and appropriate recommendations made.
			</p>
