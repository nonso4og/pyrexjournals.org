			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The rupture of the OLAPA (Oleoduto Araucária-Paranaguá) pipeline in February 2001 caused a spill of approximately 52,000 litres of diesel oil that reached rivers in the Serra do Mar region, in part, of the Atlantic Forest, which is one of the most important and fragile biomes in Brazil. The aim of the present study was to determine whether, 10 years after the rupture of the Oleoduto Araucária-Paranaguá pipeline, the fish populations at sites exposed to the spill showed changes in structure that may be correlated with the oil spill. Five rivers were selected for assessment of their ichthyofauna: the Meio and Sagrado rivers (treatment), which were exposed to oil, and the Pinto, Passa Sete and Marumbi Rivers (control), which were selected because they present environmental characteristics similar to those of the exposed rivers and are located in the same geographic region. A comparison of biometric variables between the treatment and control groups showed greater mean values for the total length, standard length and total weight of the fish captured in the treatment group. The analyses indicated no difference between the treatment and control groups in relation to the number of significant differences previously reported for the biometric variables of each studied species. These findings indicate biometric similarity between the fish populations inhabiting rivers exposed to the oil spill and those in unexposed rivers. There was no evidence of changes in the population structure of fish in the environments affected by the oil spill.
			</p>
