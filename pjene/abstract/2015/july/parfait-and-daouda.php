			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		In recent years, the quality of groundwater has deteriorated in many parts of Benin as a result of the expansion of agriculture, the release of solid waste, industrialization and population explosion. This study reports the evaluation of the vulnerability of the aquifer of the Continental terminal of the town of Abomey-Calavi. The study of vulnerability is to evaluate the sensitivity of groundwater to any form of pollutant introduced from the ground surface based on the physical properties of the medium. The DRASTIC method was applied to assess the vulnerability of the aquifer system. The results show that the study area is not homogeneous from the point of view of the vulnerability. The highest DRASTIC index concern the area of Togbin which has an aquifer of aeolian sands and marine cord current shoreline and recent unprotected by clays. While in the rest of the town of Abomey-Calavi the aquifer is less vulnerable because it is protected by clays. In this study, a map of groundwater vulnerability was produced in the town of Abomey-Calavi. The map shows two vulnerability classes. The vulnerability of the waters of the continental terminal is average in the area of Togbin and the vulnerability of waters of the continental terminal is low throughout the rest of the town of Abomey-Calavi. This card can be used as a tool for decision support and spatial analysis for water projects in the region.
			</p>
		<p>
<b>Keywords:</b> Aquifer, Groundwater vulnerability, DRASTIC method, Benin.
