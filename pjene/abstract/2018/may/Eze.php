			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Climate change is recognized presently as one of the most potential threats to man and his environment. This is particularly the case in developing countries because they are least equipped to adapt to the phenomenon and the risks associated with it. It has adversely affected agricultural productivity in Africa. The West African Sahel which includes the extremes of northern Nigeria has proven vulnerable to the impacts of climate change due to its already known history of climatic vagaries and variability’s. This paper did a desk discussion through a review of literature appraising the impact of current climatic variability and change on agricultural livelihoods in West African Sahel zeroing in on northern Nigeria. The paper recognized climate change as attributable to natural climate cycle and human activities. It observed that the West African Sahel has shown various changes especially in terms of rainfall, of which inter-annual variability is very high. It has witnessed increases in temperature and increased dry spells. These have had significant negative consequences for the poor resource farmers whose incomes and livelihood depend on rain-fed agriculture; thus jeopardizing their livelihood. Adding to this scenario is the high rate of population increases which exacerbates pressure on the already stressed land-resource environment. It is therefore recommended and advocated among others that besides the need for the intensification of efforts by development players in the region to diversify livelihood portfolios of poor rural farmers, that quality and lively efforts be made towards integrating indigenous knowledge and practices into formal climate change mitigation and adaptation strategies. 
			</p>
