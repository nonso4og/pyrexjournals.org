			<br />
			<br />
			
			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Wetland utilisation by rural communities in South Africa poses a serious challenge for natural resource conservation. The growing rate of rural poverty, poor education and socio-cultural beliefs contribute towards the current plight of many wetlands. The challenge that faces wetland sustainability stems from the over utilisation of wetland resources for survival as well as cultural activities that are practiced. This study aimed to
investigate the impact of various wetland activities performed on a daily basis by local communities of Mandlanzini in the quest for survival. The community groups were selected to study their way of life and their relation to their local environment in order to deepen the understanding of their activities and impact on the wetland. Household data gave a clear picture of socio-economic status of the community. The results of the study showed that community members, on the lower end of salary scales, were the ones who relied heavily on raising income by using resources available from the wetland. Research data showed that, there is a high demand for staple food such as amadumbe, banana, and sweet potatoes by the community under study. The study also showed that indigenous knowledge can play an important role towards the restoration of wetlands. Furthermore, as many community members practiced subsistence agriculture, most of their livestock depend on wetland resources. All these constituted good reasons why communities should be involved in wetland management and conservation.
			</p>
		<p>
<b>Keywords:</b> Agriculture, cultural practices, subsistence, sustainability and wetlands.
</p>

