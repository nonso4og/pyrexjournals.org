			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Sub-chronic toxicity test of LSE was carried out by oral administration of 25, 50 and 100mg/kgbw to healthy wister rats (Rattus novergicus) for 28 days, with appropriate immune suppressant (Dexamethasone) and immune stimulant (Jobelyn) and negative controls. Blood samples were collected from the rats and analysed for metabolite parameters including, Sodium (Na), Chloride (Cl), Potassium (K), urea and Creatinine, following standard procedures. Histopathological examination of kidney harvested from the rats, post sacrifice, involved grossing and tissue processing. The results showed that sub-chronic tests even at high concentration (100mg/kgbw) did not cause mortality nor elicited behavioral aberrations in rats. LSE had no significant (p>0.05) effect on concentrations of Na (range = 115.60±8.29 to 135.80±3.24mmol/L) and K (7.20±0.20 to 8.48±1.10mmol/L). However, Chloride, Urea and Creatinine significantly (p<0.05) increased from control concentration of 82.40±5.53 to 133.60±6.40mmol/L, 12.09±1.41 to 25.5±4.20mmol/L and 2.25±0.49 to 2.84±0.72mmol/L, respectively, at 50mg/kgbw of LSE, where significant peak concentrations were attained. The histopathological examination of kidney of rats showed a dose dependent moderate tubule-intestinal inflammation with congestion of the renal vessels. The findings of this study clearly indicated the potential physiological hazards inherent in LSE and should guide its refinement and applications in clinical conditions. 
  
			</p>
