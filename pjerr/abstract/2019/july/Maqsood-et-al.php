			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
             Objective: The objective of this study is to determine the student’s attitude towards research. The sample of students is selected from University of the Punjab. Methods: Factor analysis applied to extract important research factors. Descriptive statistics and inferential analysis is applied to have a better understanding of the students’ attitude towards research. Responses were analyzed by applying mean, Man Whitney U test and Kruskal Wallis H test. Results: Overall the students have a positive attitude towards research. The male students are more positive compared to their female counterparts. As predicted those with a high degree display a high positive attitude towards research. Conclusion: Participants of PhD and MPhil were significantly better than participants of Masters and BS (hons) on factor 1 “positive attitude towards research”, and factor 2 “research usefulness for profession”. 


</p>

