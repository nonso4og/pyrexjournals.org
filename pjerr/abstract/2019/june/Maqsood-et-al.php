			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
             To get education is the first obligation of the Muslims. The Holy Prophet (P.B.U.H) highly praised the value of education when He asked the Muslims to get knowledge even if they had to go to china. Education is a vehicle for getting factual, valid and reliable information. This study is to identify the factors that affect the academic performance of Government and Private College students at inters level in Gujranwala city. Students’ academic performance is affected by different factors such as family factors, students’ own factors, environment factors, parent’s education and income etc. These factors directly or indirectly influenced on student’s academic performance. But in this study, some new factors are used which motivates the students’ to achieve his academic goal in an excellent manner.  This study contributes to the existing body of knowledge by comparing the results of public and private college students on the basis of factors like demographic and socio economic status, intrinsic and extrinsic factors. Data is collected from 305 students. Results showed that College reputation, self-regulated learning, self-concept and academic motivation are important factors for academies not attended student’s academic marks. Academic motivation, socio economic statuses are more important factors for predicting the academies attended student’s academic marks. College administration, peer group and college facilities are less important factors for predicting academies not attended student’s academic marks. College administration and college facilities are less important for predicting the academies not attended student’s academic performances. 


</p>

