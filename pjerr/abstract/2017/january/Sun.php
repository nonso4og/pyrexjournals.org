			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
              There are quite a few questions related to entrepreneurship education (EE). For example, why do we teach entrepreneurship? What to teach? How to teach, How to assess the learning? Many previous studies answered the question about why. These papers aim to address the questions on what and how. This paper reports a PIPE Model (Problem-Idea-Product-Enterprise) which aims to motivate and inspire students to actively learn along the process from discovering problems, generating new ideas, proposing new products to planning new enterprises. It has been enriched by considering Chinese culture along the process of teaching and learning entrepreneurship. The PIPE model has been implemented at City University of Hong Kong for over 10 years and a MOOC course was recently launched based on the PIPE model. A MOOC course was designed on the PIPE model as well. It was shared in over 100 seminars and workshops in Chinese universities as well as other Asian countries. It is easier to be adopted and implemented. 


</p>

