			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
              The study sought to examine fourth year’s university student’s views and opinions on the implications of Social Networking Sites on academic performance, with specific interest being Kisii University. The increased use of SNS has become an international phenomenon in the past several years, especially among youths who have embraced these sites as paramount ways to keep in touch with their peers and share information. A survey was done in the Kisii University by distributing questionnaires to 150 sampled students from six schools, including: Faculty of Education; Business and Economics; Information and Technology; Health Sciences; Engineering and Agriculture, in addition, six heads of department were interviewed for data validation. The purpose of the study was to examine the implications of Social Networking Sites on academic performance. The research questions and objectives were: What are the SNS accessed by students on academics? Why do students prefer various SNS? How do these preferred sites influence academic performance? And, to establish the SNS accessed by the University students in academics, to find out why University students prefer various SNS, to examine the implications of SNS on academic performance. Mixed survey design was adopted and conducted in Kisii University involving 150 fourth year students and 6 heads of departments. Quantitative data were generated using questionnaires and presented in tables using frequencies and percentages while qualitative data was generated through interviews and analyzed thematically. The study was guided by technological determinism theory and social learning theory which presumes that technology has effects on society and people learn through their social interaction. The study, reveals that Social Networking Sites had effects on academic performance. 


</p>

