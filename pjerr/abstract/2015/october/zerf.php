			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
             The study aimed to reveal the needs and requirements of quality and Accreditation in lesson of physical education and sport in the reality of mixed classes vis-a-vis nature of the physical activity, which is more male than female and the tomboy as attitude that, schoolchildren think as Fashion Witnessed in the Arab society in general, and Algeria in particular. Our intervention interest in the context of the neglect of this aspect of personal teenage girl is contributing to the spread of this phenomenon, especially in physical education, which develop physical self-perception profile. From that our research sample within the basic study was consisted of 30 students volunteered to participate in this study, their average age =14 years of middle school education (Educational institution-hamezi mohammed-Mascara-Mascara-Algeria for academic year 2014-2015). In order to study phenomenon, we have distributed the sample base on the quiz test of ChubbyBubbles22 developed on: 2015-01-04. As new in our study, we have replaced subscales physical self-perception profile questions to test physical conditioning and attitude which was approved by some professors of Physical Education, University of Mostaganem in ((body fat, fitness health and self-esteem as Test Body Mass Index), (Physical conditioning. Endurance as Test 1000 meter Run) (Physical strength as Test Vertical Jump) as protocol study and methods in the confirmation of many authors: unfortunately, the models and measures in existence have been found wanting, somehow not capturing the essence of self-experience where this deficiency has motivated a new series of investigations.
To verify the hypothesis that support: Which differences and relationship can be observed between the variables Selected for study? For that, we have chosen the analysis of the Anova with the LSD and the correlation to compare the implementation differences and the relationship that can be observed between the variables Selected for study. Based on the results accuses, Age properties, the coeducation institutions, lack of rhythmic sports in the program of physical education and the only law instructions educational appropriate in recommendation uniforms to attend classes etc. After statistical treatment of the data, we confirm: (1) that mixed classes develop physical self-perception profile esteem tomboy gender identity due to the confrontation between the two sexes; (2) There is a strong relationship between physical tests (mal & tomboy) and (tomboy& girly girl) in the opposite of the relationship between (mal & girly girl). (3) Lack of rhythmic sports in the program of physical education and the confrontation between the two sexes spread this phenomenon.
</p>

<p><b>Keywords:</b> Physical self-perception profile, tomboy, esteem gender identity attitude, mixed lesson of physical education and sport.

