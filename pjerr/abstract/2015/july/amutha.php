			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
              Learners are the hub in the educational process. Every classroom consists of all the three types of learners i.e Visual learners, Auditory learners, Kinesthetic learners. Paradigm Shift has happened from teacher centered to student centered learning. The challenge of a teacher is to cater to the needs of all types of learners in every classroom. Learning styles vary according to the individuals. Heutagogy, a technique of self-determined learning with the practices and principles rooted from andragogy that could be responsible for the developments in higher education. Technology had made unprecedented changes in the area of education. Advancement of science has to be effectively conveyed to the students without much time gap. Therefore induction of interest among students to learn the basic and the latest advancement in science is essential. e-Content will provide multi-sensory experience of the learners. Also the learners will be able to visualize the entire content and attain mastery over the topics. In the present study e- content on osmosis was developed and given to the tertiary learners for learning. Pre-test and Post-test was administered to the samples to ascertain the impact of e-content. Symbiotic relationship between the learning style and the performance are explored in this paper. Results revealed that e-content with the heutagogical approach for tertiary learners were effective.
</p>

