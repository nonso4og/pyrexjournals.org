			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
              A scientific evaluation of the difficulty of language learning was carried out at the Defence School of Languages (DSL), UK for a year long intensive language course. A questionnaire survey of exploratory nature was carried out aiming at the understanding of the Pashto learners’ problems during their intensive professional language training at the DSL. Various statistical indices were calculated to correlate the difficulties in the language with other background factors affecting the language acquisition of the students while learning a second language. Results obtained from the Relative Frequency of Citation (RFC) show a very high RFC value for vocabulary and word order categories, concluding these two areas posing significant problems for the language learners at DSL. The results also suggest that still the highly salient problems for the students at DSL are the word order/sentence structure followed by vocabulary, and conditional sentences. Learning grammar ranked the second most important problem for the students at the institute. The two methods of Ordination Analysis (a multivariate analysis) applied using Canoco 4.5 for Windows were: Detrended Correspondence Analysis (DCA) and Canonical Correspondence Analysis (CCA). The DCA results for grammar learning show that subjunctive structure is at the top of the ordination plot. The results indicate vocabulary and verb conjugations can significantly, negatively affect the speaking ability of a student. It is recommended that for a systematic learning of a second language, a learner must be exposed to the basic grammar knowledge in the first language. A cognitive/cultural domain must be understood in order to develop a better learning plan. Further studies are recommended to be carried out on a wider scale, to get a better understanding of the hidden intricate relationships developed in second language learners.
</p>

<p><b>Keywords:</b>  Pashto language, Cognitive Domain, Defence School of Languages, Action research.

