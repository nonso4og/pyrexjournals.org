			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This study is intended to investigate the problems of teaching and learning free writing at the secondary level in the Sultanate of Oman. The study aims at highlighting the difficulties that face learners as well as methods for helping them to get a good mastery and command of producing free writing.  The study also emphasizes the adoption of a pupil-centered teaching and learning process taking into consideration the new trend of ELT and the integration of modern technology into teaching free writing.  In addition, much emphasis has been placed on grading and integrating the three types of composition (controlled composition- guided composition- free composition).  One hundred and eighty learners and fifty teachers were selected randomly to carry out the investigation. Two versions of a questionnaire, an observation and a writing test were selected as data gathering tools. According to the collected data, learners have much difficulty in writing English composition. It has been verified that they are weak in the form and content of composition writing.  Moreover, a great number of teachers do not adopt the appropriate techniques for teaching composition. The result of the experiment showed that by adopting group work technique and PowerPoint, learners' interest and achievement can be enhanced. In the light of these findings, the researcher, among other, that teachers should put into practice the highly recommended techniques for teaching controlled semi-controlled and free composition. Adequate motivation and contextualization are found key factors, both of which can help improve learners' achievement in composition writing. The researcher hopes that the findings of this study would be helpful and beneficial for both teachers and learners.
			</p>

