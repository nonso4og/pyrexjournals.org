			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
              The blame for the decline in standard of education has been placed at the doorstep of teachers. In search of a remedy to the appalling situation, policy makers looked towards building capacity in terms of implementing a mentoring Program design to help early career lecturers.  One of the challenges facing mentoring Programs is the process of creating successful pairs.  The purpose of this study was to determine if mind styles of mentors and mentees influenced satisfaction with the mentoring relationship. Delineator as well as the Mentoring Relationship Questionnaire was administered to elicit information on demographics, Mind Styles, and relational satisfaction from28 mentors and 25 early career lecturers. The data collected were analyzed using frequency, percentage, mean and standard deviation. The average age of mentors was 40.83 with the average Mentee being 25.28 years of age.  The mentor group was 80% male, while Mentees were 69% male.  The group was largely Concrete Sequential, but all styles did appear at least once.  Differences in relational satisfaction based on Mind Style combinations were found for three different combinations.  A large effect size was found for Concrete Sequential mentors paired with Abstract Random Mentees, indicating a difference in satisfaction.</p>

<p><b>Keywords:</b>  Mentor, Mentee, Pairs, Agricultural Education, Relationships, Mentoring, Nigeria</p>
			
