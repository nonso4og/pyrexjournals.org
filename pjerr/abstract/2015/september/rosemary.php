			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
             The study investigated the influence of personality and social factors on delinquent behavior among secondary school adolescents in Obio/Akpor LGA of Rivers State. The design of the study was an expost-facto. The sample for the study was 300 students. Two instruments were used to collect data for the study and they were Adolescent delinquent Behavior and Neo-Personality inventory with reliability of 0.77 and 0.82 respectively. Five research questions as well as five hypotheses were answered and tested for in the study. Responses for answering research question 1 were analyzed with multiple regression, those for research question 2 with beta values associated with multiple regression. Research question 3 and 4 were answered with mean and standard deviation. Hypotheses 1 was tested with ANOVA associated with multiple regression, hypotheses 2 was tested with multiple regression while hypotheses 3 and 4 were tested with independent t-test. The study found that, extraversion and openness to experience jointly had about 5.4% influence on the variations in the development of delinquent behavior, Furthermore, adolescents whose peer group is clique had a higher mean score on the development of delinquency than those whose peer group is isolates. Based on the findings; it was recommended that, educational program should be organized which will help young people learn how to engage in positive self-appraisal, deal with conflict and control aggression. Contributions to knowledge were made.
</p>

<p><b>Keywords:</b> Personality Traits, Adolescents, Delinquent Behaviour, Peer Group.

