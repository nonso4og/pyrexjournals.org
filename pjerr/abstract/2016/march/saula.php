			<h1 style="font-size:20px; " class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
             This study intends to investigate the role of adult education as a catalyst for social change in Nigeria using the Lagos State as a study. The aim of the research is to empirically find out whether adult education has had significant impact on social change in Lagos State. It will also discuss the importance of adult education as well as the challenges facing adult education in Nigeria. The research adopts descriptive research survey with a focus on adult students, offering various courses at Lagos State University and the instrument used for the study was a questionnaire developed to cover the research questions and hypotheses. One hundred respondents comprising of adult learners were randomly selected from Lagos State University in Ojo Local Government Area of Lagos State for data collection. Data were analyzed using frequency tables, percentages and Chi-square. It was suggested in the paper that adult education programs are veritable tools designed to equip adults who are the economically productive and active citizens with required knowledge, attitudes, skills and commitment needed for meaningful social change.
</p>

<p><b>Keywords:</b> Adult, Adult education, Development, Social Change.

