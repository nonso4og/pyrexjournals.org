			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The global impact of the converging dual epidemic of pulmonary tuberculosis (PTB) and Human immunodeficiency virus (HIV) is one of our major public health challenges. In this study, a total of 262 confirmed HIV positive patients were screened for co-infection with the PTB. They consist of 147 males and 115 females, with an age range from  1 to 60 years and above, hailed mainly from urban (119) and semi-urban (78) settings, mainly civil servants (61) and traders (52). Out of the HIV patients tested for PTB, 7.3% were smear-positive (co-infected) with highest prevalence 13.7% found among the age group of 31 to 40 years, males (8.2%). The HIV prevalence of 12.3% was in PTB negative patients, females (16.5%) with high rate 17.8% in 21 to 30 years group. HIV negative/PTB positive frequency of 11.6% was observed among the males. Prevalence of HIV/PTB co-infection (11.5%) was in the traders/artisans, then drivers (8.8%) with rates of (15.4%) and (19.6%) respectively for HIV only. In the control group, the highest prevalence of PTB (19.6%) was also among the traders/artisans, but followed by farmers (15.4%). Most of the patients are from urban settings, with HIV/PTB (7.6%) and HIV cases alone (13.4%), while PTB cases were more prevalent (21.5%) in those from rural areas. Patients’ clinical profile shows that prevalence of HIV/PTB co-infection (15.5%) and HIV alone (27.3%) was within patients not on HAART. A similar rate 9.4% and 12.3% were also found among patients not on TB treatment, with the co-infection also higher 12.3% in those with low CD4 cells count. The rate of HIV/PTB co-infection in our study is generally lower than the single occurrence and is associated with delayed therapy which predisposes the patients with immunosuppression.
  
			</p>
