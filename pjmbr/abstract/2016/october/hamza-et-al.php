			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The present study screened thirty lactic acid bacteria isolates from ten samples of Sudanese traditional fermented foods collected from different locations in Khartoum state for antibacterial activity. Microscopic examination revealed that all the isolates are Gram positive, twenty nine (96.3%) of these isolates were rod-shaped, and the rest (3.3%) is cocci. All isolates were subjected to a number of preliminary tests, which are commonly used for the identification of lactic acid bacteria these including morphological, physiological and biochemical. The results indicated all isolates are LAB, and most of the tested 30 isolates produced exopolysaccharides from sucrose under the conditions used. These isolates can be utilized for the production of safe fermented foods and milk products. Antimicrobial activities of the 30 isolates were tested against five pathogenic bacterial strains using the agar desk diffusion technique. Out of these 25 (83.3 %) isolates exhibited antimicrobial activity against at least one indicator strain tested, six isolates were characterized to be producers of antibacterial as evidenced by strong inhibition zones formed against sensitive organisms on agar plates, the isolates demonstrated abroad spectrum of microbial activity by inhibiting Staphylococcus aureus ATCC 43306, E. coli ATCC 25922 and Salmonella typhae. Five isolates (16.7 %) had no antimicrobial activity. The physiological results, biochemical tests and sugar fermentation profiles all gave the same results for the bioactive isolates, which were indicative of bacilli and enterococci.
  
			</p>
