			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Fifteen samples of meat product (before and after cooking) were collected from various Basrah restaurants to determine the bacterial quality. The present study was found of total aerobic  was 2.3x109,4,5x108 from hamburger and 9.2x108,6.6x104 from kebab and 4.6x108,1.2x102 from kobba and 3.5x105,2,6x102 from beer shaearma and 4.3x104,1.9x102 from tekka. And the total coliform bacteria was 6.2x104 , 8.1x102 from hamburger and 7.4x104 , 3.5x102 from kebab and 1.2x102 , 0.8x102 from kobba and 4.5x102 , 3.3x 102 from beef shawarma and 0.6x102 from tekka. Total fecallo coliform bacteria was 3.8x103 , 0.9x102 from hamburger and 6.5x104 , 0.4x102 from kebab and 0.98x102 , 0.006x102 from kobba and 3.3x104 , 0.09x104 from beef shawarma and 0.04x102 from tekka. Total psychrophilic bacteria was 4.8x102 , 0.7x102 from hamburger  and 1.6x102 , 0.06x102 from kebab and 0.6x102 , 0.08x102 from kobba and 00.9x102 from beef shawarma and 0.06x102 from tekka.
  
			</p>
