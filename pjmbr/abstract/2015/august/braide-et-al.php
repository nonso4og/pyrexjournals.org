			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Fresh cow milk often connotes an off-white nutritious liquid conjectured from the off-whitish natural fluid secreted by female mammals from the mammary gland and nunu is a fermented milk product consumed as a drink in parts of West Africa. The quality of raw milk and “nunu” was assessed by the rapid dye reduction method, comprising of methylene blue reduction (MBR) test and resazurin test. Standard microbiological methods were adopted to determine aerobic bacteria, coliforms and fungal characteristics. Total aerobic bacteria counts were 2.92 × 1010-3.2 × 109; coliform count ranged between 7.0 × 109-1.0 × 108. Fungal count was 1.45 × 1010- 4.0 × 108. The total microbial count was high, indicating gross contamination. The microorganism isolated includes species of Escherichia coli, Enterococcus, Streptococcus, Staphylococcus, Micrococcus, Bacillus, Corynebacterium and Lactobacillus for bacteria and, Mucor, Saccharomyces, Rhizopus, Fusarium, Geotrichum, Aspergillus and Penicillium species of fungi. The presence of faecal coliform and Staphylococcus aureus could account for food infection, whereas Streptococcus agalatiae suggest mastitis infection from the udder of the cow. Some of the fungal isolates have been implicated with serious mycotoxicosis of animals and man. Results obtained from the dye reduction test suggest absolute rejection of the samples. The sources of contamination could be from the milk producing animals, the milking utensils as well as the water used for ‘nunu’ processing and suitable environmental factors that favours the proliferation of contaminating microorganisms. Efforts should be intensified on improving the sanitary condition of the product to avoid fatal health hazards.
  
			</p>
