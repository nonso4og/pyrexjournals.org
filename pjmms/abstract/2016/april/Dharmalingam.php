			<h1 style="font-size:20px; color:blue;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Bukit Merah Orang Utan Island (BMOUI) serves as an ex-situ conservation facility for the endangered Bornean orangutans. The role of the Infant Care Unit (ICU) is to provide the veterinary care for infant orangutans, to create public awareness and to provide the public with an opportunity to view how the veterinary care is provided for the infant orangutan. Upper Respiratory Tract Infections (URTI) is common in infant orangutan between age one month to two years. Vital sign and treatment should be closely monitored during the recovery period. URTI may lead to lower respiratory tract infection.
			</p>

