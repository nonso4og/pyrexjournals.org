			<h1 style="font-size:20px; color:blue;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The number of falls in older people with knee osteoarthritis (KOA) is almost double the number experienced by people with no OA. The neuromuscular elements required to arrest a fall are more impaired in people with KOA compared to their asymptomatic counterparts. Therefore, these elements may need to be incorporated into an exercise intervention to reduce the risk of falling. This randomized controlled study aims to investigate the effect of balance training on fall risk in participants with knee osteoarthritis. The participants were 28 older female, (n=28, mean age= 70,76.±4.34years mean height=163.8±5.6 and mean weight=77.83±10.97)  who were graded II and III knee osteoarthritis according to the ‘‘Kellegren-Lawrence Scale’’ were categorized into treatment and control groups. Socio-demographic features were recorded. Then their balance tests were applied with Tetrax Interactive Balance System Berg Balance Scale (BBS) was applied to assess the balance of patients. No statistically significant differences were observed between the groups at baseline in terms of age, height, weight and body mass index (p > 0.05). Statistically significant differences were found in all assessed parameters in both balance and control groups (all p<0.05) in a comparison of the two groups, statistically significant differences were found in all parameters (all p<0.05). We think that appropriate physiotherapy and rehabilitation programme that is prepared for knee osteoarthritis patients will contribute the improvements about correcting patients’ balance according to this study. 
			</p>

<p>
<b>Keywords:</b> Knee, Osteoarthritis, Postural balance, fall risk.
