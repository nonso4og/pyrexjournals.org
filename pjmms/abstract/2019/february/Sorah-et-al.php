			<h1 style="font-size:20px; color:blue;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Neonatal mortality is still high during perinatal or below first month of pregnancy. Three quarters of these deaths occur in the first week of pregnancy. The causes of deaths are asphyxia, birth trauma, infection, prematurity, congenital abnormalities, and other causes. The aim of this study is to analyze the relationship between behaviour of pregnant women with preeclampsia and health status of newborns in delivery rooms of Prof. Dr. W. Z. Johannes Kupang Hospital. The study was conducted from January to March 2015. The sample was 50% of the population or 35 respondents that was taken using consecutive sampling with cross sectional study design. The research field was delivery rooms of Prof. Dr. W.Z. Johannes Kupang Hospital. The bivariate test results with Chi-Square showed that these factors was significantly related to the health status of the newborn, they are gestational age (p-value=0.031), a health history (p value=0.035), family income (p value=0.035) and weight gain (p value=0.021). Enabling factors such as health service points (p value=0.051), ANC visits (p value=0,011) and MNH book holders (p=value 0,031) also contributed significantly to health status of newborn babies. The reinforcing factors such as healthcare staff (p value=0,031) also significantly related health status of newborn babies. 
			</p>

<p>
<b>Keywords:</b> Behavioral Factors, Health Status of Newborns, Preeclampsia.
