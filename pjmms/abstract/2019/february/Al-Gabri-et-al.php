			<h1 style="font-size:20px; color:blue;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		A two-year Holstein-Friesian heifer was admitted to the Vet Clinic, Faculty of Veterinary Medicine Zagazig University. Clinical examination revealed a two tumor like pedunculated poorly demarcated masses that attached to the vaginal wall and partly protruded through the vagina. Two masses were surgically removed with some of the surrounding tissue. The dimensions of the masses were 5.2×3×2.5 and 3×2×1cm respectively. Macroscopically, well circumscribed with lobulated patterns, white creamy color with firm consistency and ulcerative surfaces were noticed. Microscopically, pleomorphic tumor cells (spindle to oval) that embedded in amorphous fibrillar stroma forming interlacing bundles were prominent. The neoplastic cells showed cigar-shaped large vesicular nuclei with karyomegaly and granular chromatin beside multinucleated giant cell formation. The mitotic indices were prominent. The neoplasm showed positive immunohistochemically for smooth muscle actin. The previous data declared of well differentiated non metastasis liomyosarcoma. 
			</p>

<p>
<b>Keywords:</b> Neoplasm, vaginal, immunohistochemistry, heifer.
