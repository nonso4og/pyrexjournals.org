			<h1 style="font-size:20px; color:blue;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		BACKGROUND: Fibroid is a common benign uterine tumour with a higher prevalence among women of African descent. The definitive treatment involves surgery. Post-operative pain management following such intervention in resource-limited settings may be challenged by paucity of skilled man-power and other infrastructural inadequacies. Different post-operative analgesia options have been explored by operators in such environments to achieve desired outcomes. CASE PRESENTATION: We report a case of 32-year-old woman who underwent myomectomy at the main theatre of the University of Port Harcourt Teaching Hospital (UPTH), Nigeria for symptomatic fibroid. Bupivacaine epidural anaesthesia was employed for the surgery. Top-up (repeat bolus) doses of bupivacaine (10mls of 0.25%) epidural analgesia and regular diclofenac, with tramadol for breakthrough pain were utilised in a multi-modal post-operative analgesia regimen, to achieve a satisfactory clinical outcome. CONCLUSION: We have presented a detailed report of an epidural-based multi-modal post-operative pain management technique following a major lower abdominal surgery. If a patient receives epidural anaesthesia for such surgical procedure, then an epidural-catheter in-situ for 1st 24 hours could be recommended for further use in the management of post-operative pain. 
			</p>

<p>
<b>Keywords:</b> ‘epidural’, ‘analgesia’, ‘myomectomy’, ‘pain-management’.
