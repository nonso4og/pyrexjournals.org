			<h1 style="font-size:20px; color:blue;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Background: hernia is a protrusion of viscus through a defect in the wall of an organ. inguinal hernia has a peak incidence in infancy and childhood and, represents about 70% of all hernias. surgery (herniotomy) is the recommended treatment for paediatric inguinal hernia. Post-operative pain relief in children poses a challenge to physicians especially in resource-limited settings. Consequently, effective and cost-beneficial post-operative analgesic regimens are essential for optimal care of children who receive surgeries in such environments. Case presentation: we report a case of master b, a 30-month-old boy who had herniotomy at the main theatre of federal medical centre, umuahia, abia state, nigeria for inguinal hernia of 7 months duration. bupivacaine caudal analgesia (with neostigmine adjuvant), nitrous oxide and halothane were employed for surgical anaesthesia. Post-operatively, a single dose of diclofenac suppository (25mg) and regular paracetamol were employed to achieve effective post-operative analgesia. Conclusion: we presented caudal anaesthesia/ analgesia, diclofenac suppository and paracetamol as co-analgesic regimen for post-herniotomy pain relief. If a child received caudal anaesthesia/analgesia for herniotomy, such non-opioid combined regimen could be recommended for effective post-operative analgesia. 
			</p>

