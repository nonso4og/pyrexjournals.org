			<h1 style="font-size:20px; color:blue;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		The Research Aim is to design the implementation model of maternal and child health policy in East Nusa Tenggara, Indonesia. The Research Method is an Analytic Observational with cross sectional design. The total sample of the research is 235 with the technical research of Multistage random sampling by clusters on the islands of Flores, Sumba, Timor (including Sabu and Rote), and the District of Alor then a cluster to choose the district, and perform a random pick to a Community Health Centres. A Data analysis technique using the linear regression and the equations of Structural Equation Modelling (SEM), the results showed that the variable standard and its purpose providing a positive and significant effect on the performance of the implementation of maternal and child health policy at East Nusa Tenggara, Indonesia (t count = 11.570 > t table 1.97). Resources provided a positive and significant impact on the performance of policy implementation (T arithmetic = 13.304 > t table 1.97). Characteristics of the Executing Agency gave a positive and significant impact on the performance of maternal and child health policies in EAST NUSA TENGGARA, INDONESIA (t count = 8.276 > t table 1.97). The interest of the target group had a positive and significant impact on the performance of maternal and child health policies in East Nusa Tenggara, Indonesia (t count = 9.535 > t table 1.97). The external environment gave a positive and significant influence on the performance of the policy (t arithmetic = 8.902 > t table 1.97). Geographically gave a positive and significant influence on the performance of the policy (t arithmetic = 19,0115 > T table 1.97). Communication between organizations gave a positive and significant impact on policy performance (t arithmetic = 7,299 > t table 1.97). The disposition gave a positive and significant influence on the performance of the policy (t count = 8.170 > t table 1.97). Standards and Objectives, Resources, Characteristics of Executing Agency, Target Group, External Environment, Geographical Condition, Policy Communication, Policy Disposition, Bureaucratic Structure, together they gave positive and significant influence to the Performance of the Policy (22.09 F count > F table 1.992). The conclusion of this study is the Standard and Objectives, Resources, Characteristics of the Executing Agency, The Interests Of Target Groups, External Environmental (Culture), Geography, Communications, Disposition, Bureaucratic Structure are individually/partially and simultaneously or together have a positive and significant impact on the performance of the implementation of maternal and child health policies in East Nusa Tenggara, Indonesia. It is suggested that Implementation of maternal and child health policy must consider the variables that affect the implementation of its Policy. Every variable that affects should be given more attention and support so that the expectation of the policy is achieved.
			</p>

