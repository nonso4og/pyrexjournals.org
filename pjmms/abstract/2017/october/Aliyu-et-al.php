			<h1 style="font-size:20px; color:blue;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Background: The management of typhoid ileal perforation in this low socioeconomic environment is really challenging because the population is predominantly peasant farmers lacking social amenities and quality water supply. Patients and Methods: The study retrospectively reviewed all patients diagnosed with typhoid perforation and managed in Damaturu and Potiskum District Hospitals North-Eastern Nigeria between January 2012 and December 2016 Results. A total of 279 patients were seen, 268 analyzed and 11 were excluded due to incomplete records. Age ranged from 3 to 50 years with male to female ration of 1.1:1 and a mean of 14.75 years. The peak age group was 11-20 years, accounting for 39.93%. The clinical features were abdominal pain in 99.25% and abdominal tenderness in all patients. Procedures done were simple, such as the closure of perforation in 87.31% patients, resection and anastomosis in 70.84%. Postoperative complications were surgical site infection in 21.64%, and renal failure in 1.12%. The mortality was 14.18%. Conclusion: Typhoid perforation posed a major health hazard with high morbidity and mortality, especially in a low socioeconomic environment where poor personal hygiene, improper human waste disposal, malnutrition, and poor social amenities exist. Despite high mortality associated with the disease, timely diagnosis, aggressive resuscitative measures as well as early surgical intervention can go a long way in reducing menace of the disease. Prevention is the most effective way of reducing this public health problem through health education and the provision of adequate potable water.
			</p>

