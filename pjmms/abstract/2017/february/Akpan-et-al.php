			<h1 style="font-size:20px; color:blue;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		This study was carried out to assess the prevalence of urinary Schistosomiasis among primary school children in Nkarasi and Edor Communities. Both are adjoining communities in the Ikom Local Government Area of Cross River State, Nigeria. Urine specimens were collected based on convenience and consent from 246 male and female pupils aged between 5 and 15 years in primary 1 to 6 classes between 10 a.m. and 1 p.m. during each visit. The study was conducted between February and April, 2012. On-the-spot detection of proteinuria and haematuria was carried out on the urine specimens using Combur 9 urinalysis strips. In the laboratory, 10 millilitres aliquot of each urine specimen was placed in a centrifuge tube and spun at 3,000 revolutions per minute for 5 minutes. From the sediment; wet preparations were made on clean slides and examined for eggs of Schistosoma haematobium using x 10 and x 40 objective lenses. Out of 246 pupils, 134 (54.5%) were screened from Nkarasi Primary School (NPS) while 112 pupils (45.5%) were screened from Edor Primary School (EPS). Only 2 pupils (both male) from NPS were positive for eggs of S. haematobium. This result shows a prevalence rate of 1.5% at NPS. Also, 2 pupils (one male and one female) from EPS were positive for eggs of S. haematobium with a prevalence rate of 1.8%. An overall prevalence rate of 1.6% (4:246) was recorded for urinary schistosomiasis in both communities. There was no statistically significant difference in the prevalence of urinary schistosomiasis in both communities (P > 0.05; X2 = 9.84). Female pupils were more infected than their male counterparts in Edor community. No infection was recorded among the 70 female pupils in Nkarasi community, whereas male pupils aged 9 to 12 years were infected at a prevalence rate of 5.3%. Overall, there was no statistically significant difference in the prevalence of infection among male and female pupils in both communities (P > 0.05; X2 = 4.32). Generally, there was a low egg count between 6 and 25 eggs per 10 millilitres of urine. Chi Square showed no statistically significant difference in mean egg count between male and female pupils (P > 0.05; X2= 6.68). The highest values for proteinuria (43.5 mg/dl) and haematuria (14.5 ery/µl) were detected among the 13 to 16 years age-group. This study has recorded a low prevalence of urinary schistosomiasis among school children in these two communities. It also shows that the prevalence of the disease in Ikom is low, compared with other Local Government Areas in Northern Cross River State, where the disease is endemic. A sustained public health campaign will help to further reduce the prevalence of this disease in the study area and the adjoining communities. 
			</p>

