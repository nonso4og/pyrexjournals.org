			<h1 style="font-size:20px;" class="box-header-blue">About | PJMMS </h1>
			<br />
			<p>
			<b>Pyrex Journal of  Medicine and Medical Sciences </b> is an open access academic refereed journal published monthly by Pyrex Journals. <b>ISSN: 2579-1230</b> 
			</p>
			
			<p>
			<b>PJMMS</b> publishes research articles that report premier fundamental discoveries and inventions, and the applications of those discoveries, unconfined by traditional discipline barriers. All articles published by <b>PJMMS</b> are published in English. 
			</p>
			
			<p>
			The objective of <b>PJMMS</b> is to publish the most significant as well as innovative articles all areas medicine as well as medical sciences; in other to accelerate the sharing and free-access of knowledge.
			</p>
			
			<p>
			The scope of the journal is broad and it includes but not limited to the following areas of specialty:
			</p>
			
			
		<h1 style="font-size:18px; " class="box-header-blue">a. General Medicine</h1>
			
			<p>
Addictive Diseases, Antibody Therapy, Application of Medical, Surgical, Public Health, Dental, Diagnostic and Therapeutic Principles to Non-human Animals, Biomedical Engineering, Cancer Prevention, Cardiovascular Research, Chemotheraphy, Clinical Cancer Research, Clinical Cardiovascular Research, Clinical Chemistry And Biochemistry, Clinical Endocrinology, Clinical Immunology and Immunopathology, Clinical Pathophysiology, Clinical Pharmacology, Congenital Disorders, Dentistry, Dermatology, Diagnostic, Geriatrics and Sport Medicine, Experimental Cancer Research, Ethology, Forensic Pathology, Gynaecology, Hormone Therapy, Hypoglycemia in Diabetes, Infectious Diseases, Internal Medicine, Mammography, Neuroendocrinology, Neurology, Neurophysiology and Brain Research, Nutritional Research, Ophthalmology, Otorhinolaryngology, Pediatrics, Pathology, Psychosomatic Diseases, Psychiatry, Physiology, Public Health, Radiobiology, Respiratory Diseases, Surgery, Tropical Medicine, Tuberculosis and Vitaminology, etc.
			</p>
			
			<h1 style="font-size:18px; " class="box-header-blue">b. Social Medicine</h1>
 
 			<p>
			Antiretroviral Therapy, Comparative Tolerability, Dietetics, Environmental Toxicology, Ergonomy, Gender-Based HIV and AIDS Prevention Interventions, Group Medicine, Health Education, Health Services and Medical Statistics, HIV and Sexual Risk Behaviours, Medico-Social Problems of the Elderly, Medico-Social Problems of the Youth, Occupational Medicine, Rehabilitation, Human Ecology, Social Paediatrics, etc. 
			</p>
			
			<p>
		All articles that are submitted to this journal are made to undergo sectioning to ensure that they are in line with the scope of PJMMS and as well as the instructions for authors. Thereafter they are double-blindedly peer-reviewed by both internal and external reviewers. More than 100 reviewers are working diligently to shorten the review cycle of the academic research publication; and help researchers to pay more attention on the research itself, not waiting for too long, for suggestions from reviewers.
			</p>
			
			<p>
			To be accepted, a paper must be judged to be truly outstanding in its field and to be of interest to a wide audience. The primary criteria during the review and selection process of the submitted papers are; sound theoretical basis, valid empirical application and analysis, and contribution to the fields of PJMMS. The primary criteria in the final selection of the papers once the review process is complete are quality, originality, and relevance to the PJMMS fields.
			</p>
 

