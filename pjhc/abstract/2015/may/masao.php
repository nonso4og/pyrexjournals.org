			<h1 style="font-size:20px;" class="box-header-blue">Abstract</h1>
				<hr />
			
			<p>
		Compared to the rest of Tanzania, especially northern, central and coastal Tanzania where there is good knowledge of the archaeological potential, southeastern Tanzania is very poorly known. Until the current investigation, this part of Tanzania was so much dominated by Historical, archaeological studies championed by Chittick’s detailed publications on the Kilwa Monuments and later followed by scholars from the University of Dar es Salaam,that it was assumed there were no earlier human activities (Chittick 1966, 1974, Chami 2009, Chami, et al 2001,  Chami, 2001, Matiyas 2001, Msemwa  2001, Wafula, 2001, Kwekason 2011).  Not only has the current research on the Tanzanian Makonde Plateau and its outliers proved such notions wrong, but it has shown that there are assemblages which, subsequent work might perhaps show technological and typological resemblances to southern African and northern Mozambique MSA/LSA mode of existence.  For example, recent research in Mozambique has demonstrated exploitation of wild cereal by the MSA folks (Mercader et al 2008). The assemblages reported here exhibit unequivocal MSA and LSA affinities and seem to be distributed in the northern hill tops and slopes within the Makonde Plateau.  Iron Age artifacts in the form of pottery are also present but rare.  However, this report deals particularly with the MSA/LSA in Nachingwea, Masasi and Newala Districts in S.E. Tanzania. The analysis, though preliminary, indicates that these assemblages are dominated by Middle Stone Age (MSA) tools, and an increasing frequency of Later Stone Age (LSA) bladelet production, and are therefore transitional between the MSA and the LSA. As has been argued by some authors, occurrence and recognition of   MSA and LSA transitional assemblages suggests that no cut and dried classification into MSA or LSA can be applied to all late Pleistocene assemblages and supports a gradualist view of the MSA/LSA transition in this region (Clark 1997).  It is hoped further fieldwork might generate more spatially comparable data that would justify the creation of an informal name for such southern Tanzanian industries, the Masasian. 
			</p>
		<p>
<b>Keywords:</b> South east Tanzania, Makonde Plateau, MSA/LSA assemblages.
